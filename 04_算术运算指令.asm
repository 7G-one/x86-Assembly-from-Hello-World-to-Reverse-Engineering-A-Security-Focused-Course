; ============================================================================
; 一、加法指令 ADD
; ============================================================================
;
; 语法：ADD 目标, 源
; 作用：目标 = 目标 + 源
;
; 生活类比：就像在计算器上按 "+" 键

section .data
    result  dd 0
    val_a   dd 100
    val_b   dd 50

section .text
    extern _ExitProcess@4
    global _start

_start:
    ; ==================================================================
    ; ADD 基本用法
    ; ==================================================================

    mov     eax, 10
    add     eax, 5              ; EAX = 10 + 5 = 15
                                ; ADD 会修改标志位！

    ; 寄存器 + 寄存器
    mov     ebx, 100
    add     eax, ebx            ; EAX = 15 + 100 = 115

    ; 寄存器 + 内存
    add     eax, [val_a]        ; EAX = 115 + 100 = 215

    ; 内存 + 立即数
    add     dword [result], 10  ; result = 0 + 10 = 10
                                ; 注意：内存操作必须指定大小

    ; ==================================================================
    ; ADD 对标志位的影响
    ; ==================================================================

    ; 零标志 ZF
    mov     eax, 5
    add     eax, -5             ; 5 + (-5) = 0 -> ZF = 1（结果为零）
                                ; 注意：-5 在计算机中用补码表示

    ; 进位标志 CF
    mov     al, 255             ; 8位最大无符号值
    add     al, 1               ; 255 + 1 = 256，但8位装不下
                                ; AL = 0, CF = 1（有进位）
                                ; 就像汽车里程表归零

    ; 溢出标志 OF
    mov     al, 127             ; 8位最大有符号正数
    add     al, 1               ; 127 + 1 = 128？不！
                                ; 有符号解读：AL = -128
                                ; OF = 1（正溢出）
                                ; 两个正数相加得到负数 = 溢出

    ; 符号标志 SF
    mov     eax, 3
    add     eax, -10            ; 3 + (-10) = -7
                                ; SF = 1（结果为负）

    ; ==================================================================
    ; ADC - 带进位的加法
    ; ==================================================================
    ; ADC 目标, 源  =>  目标 = 目标 + 源 + CF
    ; 用于大数加法（超过32位的数）

    ; 例子：64位加法（高32位在EDX:EAX，低32位在EBX:ECX）
    mov     eax, 0xFFFFFFFF     ; 低32位最大值
    mov     edx, 0x00000001     ; 高32位 = 1
                                ; 64位数 = 0x00000001FFFFFFFF

    mov     ecx, 0x00000001     ; 要加的低32位
    mov     ebx, 0x00000000     ; 要加的高32位

    add     eax, ecx            ; 低32位相加：0xFFFFFFFF + 1 = 0
                                ; 产生进位，CF = 1
    adc     edx, ebx            ; 高32位相加 + 进位：
                                ; 0x00000001 + 0 + 1 = 0x00000002
                                ; 结果：EDX:EAX = 0x0000000200000000

    ; ==================================================================
    ; SUB 减法指令
    ; ==================================================================
    ; SUB 目标, 源  =>  目标 = 目标 - 源

    mov     eax, 100
    sub     eax, 30             ; EAX = 100 - 30 = 70

    sub     eax, [val_b]        ; EAX = 70 - 50 = 20

    ; SUB 对标志位的影响
    mov     eax, 5
    sub     eax, 5              ; 5 - 5 = 0 -> ZF = 1

    mov     eax, 0
    sub     eax, 1              ; 0 - 1 = -1
                                ; 无符号：0xFFFFFFFF, CF = 1（借位）
                                ; 有符号：-1, SF = 1

    ; ==================================================================
    ; SBB - 带借位的减法
    ; ==================================================================
    ; SBB 目标, 源  =>  目标 = 目标 - 源 - CF
    ; 用于大数减法

    ; ==================================================================
    ; INC 和 DEC - 自增/自减
    ; ==================================================================
    ; INC 目标  =>  目标 = 目标 + 1
    ; DEC 目标  =>  目标 = 目标 - 1
    ; 注意：INC/DEC 不影响 CF（进位标志）！

    mov     eax, 0
    inc     eax                 ; EAX = 1
    inc     eax                 ; EAX = 2
    dec     eax                 ; EAX = 1

    ; INC/DEC 常用于循环计数
    mov     ecx, 0
    ; 循环体：
    inc     ecx                 ; 计数器加1
    cmp     ecx, 10             ; 比较是否到10
    ; jne loop_start           ; 没到就继续循环（后面课程讲跳转）

    ; ==================================================================
    ; NEG - 取反（求负数）
    ; ==================================================================
    ; NEG 目标  =>  目标 = 0 - 目标（即取相反数）

    mov     eax, 42
    neg     eax                 ; EAX = -42
                                ; 二进制：0xFFFFFFD6

    neg     eax                 ; EAX = 42（再次取反恢复）

    ; NEG 的本质：按位取反 + 1（补码）
    mov     eax, 1              ; 0x00000001
    not     eax                 ; 按位取反：0xFFFFFFFE
    add     eax, 1              ; 加1：0xFFFFFFFF = -1
    ; 这就是 NEG 的工作原理！

    ; ==================================================================
    ; MUL - 无符号乘法
    ; ==================================================================
    ; 格式：MUL 操作数
    ;
    ; 8位乘法：  AX = AL * 操作数
    ; 16位乘法： DX:AX = AX * 操作数
    ; 32位乘法： EDX:EAX = EAX * 操作数
    ;
    ; 注意：MUL 只有一个操作数！另一个操作数固定是 EAX/AX/AL
    ;
    ; 生活类比：
    ;   MUL 就像一个特殊的乘法机器：
    ;   你把一个数放在传送带上（操作数），
    ;   另一个数必须放在机器内部（EAX），
    ;   结果从两个出口出来（EDX:EAX）

    ; 8位乘法
    mov     al, 20
    mov     bl, 10
    mul     bl                  ; AX = AL * BL = 20 * 10 = 200
                                ; 结果放在AX（16位），因为可能超过8位

    ; 32位乘法
    mov     eax, 100000
    mov     ecx, 200000
    mul     ecx                 ; EDX:EAX = EAX * ECX
                                ; = 100000 * 200000 = 20000000000
                                ; EAX = 低32位，EDX = 高32位
                                ; 因为结果可能超过32位，所以用64位存放

    ; 注意：MUL 溢出时 CF=OF=1
    mov     al, 200
    mov     bl, 200
    mul     bl                  ; AX = 200*200 = 40000
                                ; 但AL*BL的结果本应放在AX
                                ; 40000 < 65536 所以没问题
                                ; 如果结果超过AX能表示的范围，CF=OF=1

    ; ==================================================================
    ; IMUL - 有符号乘法
    ; ==================================================================
    ; IMUL 有三种形式：
    ;   1. IMUL 操作数        （同MUL，但有符号）
    ;   2. IMUL 目标, 源      （目标 = 目标 * 源）
    ;   3. IMUL 目标, 源, 立即数 （目标 = 源 * 立即数）

    ; 形式1：单操作数
    mov     eax, -10
    mov     ecx, 5
    imul    ecx                 ; EDX:EAX = EAX * ECX = -50

    ; 形式2：双操作数
    mov     eax, 10
    imul    eax, ecx            ; EAX = EAX * ECX = 10 * 5 = 50

    ; 形式3：三操作数
    imul    ebx, eax, 7         ; EBX = EAX * 7 = 50 * 7 = 350

    ; ==================================================================
    ; DIV - 无符号除法
    ; ==================================================================
    ; 格式：DIV 操作数
    ;
    ; 8位除法：  AL = AX / 操作数 的商,  AH = 余数
    ; 16位除法： AX = DX:AX / 操作数 的商,  DX = 余数
    ; 32位除法： EAX = EDX:EAX / 操作数 的商,  EDX = 余数
    ;
    ; 生活类比：
    ;   DIV 就像分蛋糕：
    ;   EDX:EAX 是整个蛋糕（被除数，可能很大）
    ;   操作数 是要分给几个人（除数）
    ;   EAX = 每人分多少（商）
    ;   EDX = 剩下多少（余数）

    ; 32位除法示例
    mov     edx, 0              ; 高32位 = 0（被除数高32位）
    mov     eax, 100            ; 低32位 = 100（被除数）
    mov     ecx, 7              ; 除数 = 7
    div     ecx                 ; EAX = 100 / 7 = 14（商）
                                ; EDX = 100 % 7 = 2（余数）

    ; 8位除法示例
    mov     ax, 1000            ; 被除数（16位）
    mov     bl, 10              ; 除数（8位）
    div     bl                  ; AL = 1000 / 10 = 100（商）
                                ; AH = 1000 % 10 = 0（余数）

    ; ==================================================================
    ; IDIV - 有符号除法
    ; ==================================================================
    ; 用法同 DIV，但操作数是有符号的

    mov     edx, 0
    mov     eax, -100
    mov     ecx, 7
    idiv    ecx                 ; EAX = -100 / 7 = -14（商）
                                ; EDX = -100 % 7 = -2（余数）
                                ; 注意：余数的符号和被除数相同

    ; ==================================================================
    ; 注意事项
    ; ==================================================================
    ; 1. DIV/IDIV 除以0会触发异常（程序崩溃）
    ; 2. 商太大（溢出）也会触发异常
    ; 3. 做除法前一定要清零EDX（无符号除法时）

    ; 安全的除法流程
    mov     eax, 100
    xor     edx, edx            ; 清零EDX（重要！）
    mov     ecx, 3
    test    ecx, ecx            ; 检查除数是否为0
    jz      .div_error          ; 如果为0，跳转到错误处理（后面学）
    div     ecx                 ; 正常除法
.div_error:

    ; ==================================================================
    ; 实用示例：计算 (a*b + c) / d
    ; ==================================================================
    ; 假设 a=10, b=20, c=50, d=7

    mov     eax, 10             ; a = 10
    imul    eax, 20             ; EAX = a * b = 200
    add     eax, 50             ; EAX = a*b + c = 250
    cdq                         ; 符号扩展 EAX -> EDX:EAX
                                ; 如果EAX是正数，EDX=0
                                ; 如果EAX是负数，EDX=0xFFFFFFFF
    mov     ecx, 7
    idiv    ecx                 ; EAX = 250 / 7 = 35
                                ; EDX = 250 % 7 = 5

    ; 退出程序
    push    0
    call    _ExitProcess@4

; ============================================================================
; 练习题
; ============================================================================
;
; 练习1（计算题）：
;   写出以下代码执行后EAX的值：
;   mov eax, 50
;   add eax, 30
;   sub eax, 20
;   inc eax
;
; 练习2（代码题）：
;   用汇编计算 1 + 2 + 3 + ... + 100 的和，结果放在EAX中。
;   （提示：用循环，或者直接用等差数列求和公式 n*(n+1)/2）
;
; 练习3（概念题）：
;   MUL 和 IMUL 有什么区别？如果要计算 (-5) * 3，应该用哪个？
;
; 练习4（代码题）：
;   计算 1234567 * 8901234 的结果（64位），结果放在 EDX:EAX 中。
;
; 练习5（思考题）：
;   为什么 DIV 之前必须清零 EDX？如果不清零会怎样？
;
; ============================================================================
; 参考答案
; ============================================================================
;
; 答案1：
;   50 + 30 = 80
;   80 - 20 = 60
;   60 + 1 = 61
;   EAX = 61
;
; 答案2：
;   ; 方法1：循环
;   xor eax, eax            ; sum = 0
;   mov ecx, 1              ; i = 1
;   .loop:
;       add eax, ecx        ; sum += i
;       inc ecx             ; i++
;       cmp ecx, 101        ; i <= 100?
;       jle .loop           ; 如果是，继续循环
;   ; EAX = 5050
;
;   ; 方法2：公式 n*(n+1)/2
;   mov eax, 100
;   inc eax                 ; EAX = 101
;   imul eax, 100           ; EAX = 100 * 101 = 10100
;   shr eax, 1              ; EAX = 10100 / 2 = 5050
;
; 答案3：
;   MUL 是无符号乘法，IMUL 是有符号乘法
;   计算 (-5) * 3 应该用 IMUL：
;   mov eax, -5
;   mov ecx, 3
;   imul ecx                ; EDX:EAX = -15
;
; 答案4：
;   mov eax, 1234567
;   mov ecx, 8901234
;   mul ecx                 ; EDX:EAX = 1234567 * 8901234
;   ; MUL 会自动处理64位结果
;
; 答案5：
;   因为 DIV 使用 EDX:EAX 作为被除数（64位）。
;   如果 EDX 不清零，它里面的垃圾数据会被当作被除数的高32位，
;   导致被除数远大于预期，商可能溢出（触发异常）或结果错误。
;   例如：EDX=0x00000001, EAX=0 时，被除数是 4294967296，不是0！
;
; ============================================================================
; 教授的话
; ============================================================================
;
; 【核心收获】
;
;   1. ADD/SUB 加减法，结果影响 ZF/CF/SF/OF 标志位
;   2. INC/DEC 自增自减（注意：不影响 CF 进位标志！）
;   3. MUL 无符号乘法，IMUL 有符号乘法，结果在 EDX:EAX
;   4. DIV 无符号除法：商在 EAX，余数在 EDX
;   5. NEG 求相反数 = 按位取反 + 1（补码）
;   6. cdq 指令：EAX 符号扩展到 EDX:EAX（有符号除法前必用）
;
; 【常见陷阱】
;
;   1. DIV 前必须清零 EDX（xor edx, edx），否则被除数错误
;   2. 除以0会触发异常（程序崩溃），做除法前要检查除数
;   3. MUL 只有一个操作数，另一个固定是 EAX/AX/AL
;   4. INC/DEC 不影响 CF，所以在涉及进位判断的循环中要小心
;
