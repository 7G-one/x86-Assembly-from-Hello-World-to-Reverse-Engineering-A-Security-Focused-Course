; 三、字节序（Endianness）
; ============================================================================
;
; 多字节数据在内存中的存放顺序有两种方式：
;
; 小端序(Little Endian)：低字节存放在低地址（Intel x86使用这种！）
; 大端序(Big Endian)：  高字节存放在低地址（网络协议通常使用这种）
;
; 举例：存放 0x12345678 到地址 0x1000
;
; 小端序（x86）：
;   地址  0x1000  0x1001  0x1002  0x1003
;   内容  0x78    0x56    0x34    0x12
;   （最低的78放在最低的地址）
;
; 大端序（网络序）：
;   地址  0x1000  0x1001  0x1002  0x1003
;   内容  0x12    0x34    0x56    0x78
;   （最高的12放在最低的地址）
;
; 生活类比：
;   小端序 = 你从右边开始写日期：日/月/年（日是最小单位）
;   大端序 = 你从左边开始写日期：年/月/日（年是最大单位）
;
; 为什么x86用小端序？
;   历史原因。好处是类型转换很方便：
;   把一个32位整数的地址当作16位整数的地址来读，直接读到的就是低16位。
;
; 什么时候需要关心字节序？
;   - 网络编程（网络序是大端，需要转换）
;   - 读取二进制文件格式
;   - 跨平台数据交换

; ============================================================================
; 四、内存布局
; ============================================================================
;
; 一个Windows程序的内存布局大致如下：
;
;   高地址 (0xFFFFFFFF)
;   ┌─────────────────────┐
;   │     内核空间         │  用户程序不能访问
;   ├─────────────────────┤
;   │       栈(Stack)      │  向下增长 ↓
;   │   局部变量、返回地址  │  ESP指向栈顶
;   ├─────────────────────┤
;   │         ...          │  空闲区域
;   ├─────────────────────┤
;   │     堆(Heap)         │  向上增长 ↑
;   │   动态分配的内存      │  malloc/new分配
;   ├─────────────────────┤
;   │      .bss 段         │  未初始化数据
;   ├─────────────────────┤
;   │      .data 段        │  已初始化数据
;   ├─────────────────────┤
;   │      .text 段        │  可执行代码（只读）
;   ├─────────────────────┤
;   │     保留区域          │
;   低地址 (0x00000000)
;
; 生活类比：
;   .text = 工厂的操作手册（只读，工人按手册操作）
;   .data = 原材料仓库（已初始化，有东西在里面）
;   .bss  = 空仓库（预留了但还没放东西）
;   堆   = 临时租用的仓库（按需申请）
;   栈   = 工人的工作台（先进后出，自动管理）

; ============================================================================
; 练习题
; ============================================================================
;
; 练习1（代码题）：
;   在 .data 段定义一个包含你生日的字节数组（如 1999, 12, 25 -> db 99,12,25）
;   然后在 .text 段用 mov 指令把第一个字节读入 AL。
;
; 练习2（概念题）：
;   以下数据定义各占多少字节？
;   a) db 'Hello'
;   b) dw 1, 2, 3
;   c) dd 100
;   d) dq 3.14
;   e) db 5 dup('AB')
;
; 练习3（字节序题）：
;   如果在内存地址 0x1000 处存放 dw 0x1234，
;   在x86小端序下，[0x1000] 和 [0x1001] 分别是什么值？
;
; 练习4（地址计算题）：
;   给定数组：my_arr dd 10, 20, 30, 40, 50
;   写出访问 my_arr[4] 的内存操作数（方括号形式）。
;
; 练习5（思考题）：
;   .data 和 .bss 段有什么区别？为什么要有 .bss 段？
;   （提示：想想如果定义一个10000字节的全零数组，放在.data和.bss有什么不同）
;
; ============================================================================
; 参考答案
; ============================================================================
;
; 答案1：
;   section .data
;       birthday db 99, 12, 25
;   section .text
;       mov al, [birthday]   ; AL = 99
;
; 答案2：
;   a) 5字节（5个字符各1字节）
;   b) 6字节（3个字，每个2字节）
;   c) 4字节（1个双字）
;   d) 8字节（1个四字）
;   e) 10字节（重复5次'AB'，每次2字节）
;
; 答案3：
;   0x1234 = 0x12 * 256 + 0x34
;   小端序：低字节在低地址
;   [0x1000] = 0x34（低字节）
;   [0x1001] = 0x12（高字节）
;
; 答案4：
;   [my_arr + 4*4] 或 [my_arr + 16]
;   因为每个dd元素4字节，第4个元素（索引4）偏移 = 4*4 = 16
;
; 答案5：
;   .data 存放已初始化数据，会写入exe文件（占用文件大小）
;   .bss 存放未初始化数据，只记录大小不写入exe（节省文件大小）
;   10000字节的全零数组放在.data会让exe文件大10000字节
;   放在.bss只记录"需要10000字节"，exe文件几乎不变大
;   所以大量初始化为0的数据应该放在.bss
;
; ============================================================================
; 教授的话
; ============================================================================
;
; 【核心收获】
;
;   1. DB(1字节)/DW(2字节)/DD(4字节)/DQ(8字节) 定义数据
;   2. 方括号 [] 访问内存内容，LEA 获取地址（不访问内存）
;   3. x86小端序：低字节存低地址（0x12345678 存为 78 56 34 12）
;   4. .data(已初始化) / .bss(未初始化，不占exe空间) / .text(代码)
;   5. dup 重复定义：db 10 dup(0) = 10个连续的0
;
; 【常见陷阱】
;
;   1. mov eax, [myvar] 取内容，lea eax, [myvar] 取地址，别搞混
;   2. 大量全零数据放 .bss 而非 .data，否则exe文件会很大
;   3. 字节序在网络编程和二进制文件解析时必须注意
;   4. equ 定义的常量不占内存空间，编译时直接替换
;
; 【下节课预告】
;
