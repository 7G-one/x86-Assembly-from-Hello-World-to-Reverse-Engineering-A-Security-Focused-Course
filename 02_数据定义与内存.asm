; 一、内存是什么？
; ============================================================================
;
; 内存(RAM)是计算机的"主仓库"，程序运行时所有数据都在这里。
;
; 生活类比：
;   内存就像一个巨大的仓库，被分成了无数个小格子。
;   每个格子有唯一的编号（地址），每个格子能存放1字节(8位)。
;
;   ┌─────┬─────┬─────┬─────┬─────┬─────┬─────┬─────┐
;   │ 0x0 │ 0x1 │ 0x2 │ 0x3 │ 0x4 │ 0x5 │ ... │ ... │
;   ├─────┼─────┼─────┼─────┼─────┼─────┼─────┼─────┤
;   │ 48  │ 65  │ 6C  │ 6C  │ 6F  │ 00  │ ... │ ... │
;   └─────┴─────┴─────┴─────┴─────┴─────┴─────┴─────┘
;     'H'   'e'   'l'   'l'   'o'   '\0'
;
; 内存特点：
;   1. 容量大（通常几GB到几十GB）
;   2. 速度比寄存器慢（但比硬盘快得多）
;   3. 断电后数据丢失（易失性）
;   4. 每个字节都有唯一的地址
;
; ============================================================================
; 二、NASM数据定义指令
; ============================================================================
;
; NASM提供了以下指令来在内存中定义数据：
;
; 指令  全称              大小      范围
; ----  ----              ----      ----
; DB    Define Byte       1字节     0~255 (无符号) 或 -128~127 (有符号)
; DW    Define Word       2字节     0~65535
; DD    Define Dword      4字节     0~4294967295
; DQ    Define Qword      8字节     更大的数
; DT    Define Ten bytes  10字节    扩展精度浮点数
;
; 生活类比：
;   DB = 小盒子（放1个字节的东西）
;   DW = 中盒子（放2个字节的东西）
;   DD = 大盒子（放4个字节的东西）
;   DQ = 超大盒子（放8个字节的东西）
;
; --------------------------------------------------------------------------
; 代码演示：各种数据定义
; --------------------------------------------------------------------------

section .data
    ; ==================================================================
    ; DB - 定义字节数据
    ; ==================================================================

    ; 定义单个字节
    my_byte     db 42               ; 一个字节，值为42
    hex_byte    db 0xFF             ; 用十六进制定义，值为255
    char_a      db 'A'              ; 字符'A'，ASCII码为65
    null_byte   db 0                ; 空字节（字符串结束符）

    ; 定义多个字节（数组）
    bytes_arr   db 1, 2, 3, 4, 5   ; 5个字节的数组
                ; 内存布局：[01][02][03][04][05]

    ; 定义字符串（本质就是字节数组）
    greeting    db 'Hello!', 0      ; 字符串 + 空终止符
                ; 内存布局：['H']['e']['l']['l']['o']['!'][0]

    ; 用逗号分隔和不分隔的区别
    str1        db 'AB'             ; 2个字节：0x41, 0x42
    str2        db 'A', 'B'         ; 也是2个字节：0x41, 0x42（效果相同）

    ; 重复定义
    zeros       db 10 dup(0)       ; 10个连续的0
                                ; dup = duplicate（重复）
                                ; 类比：10个空盒子排成一排
    pattern     db 5 dup(0xAA, 0xBB) ; 重复5次 AA BB，共10个字节
                                ; 内存：AA BB AA BB AA BB AA BB AA BB

    ; ==================================================================
    ; DW - 定义字(Word)数据（2字节）
    ; ==================================================================

    my_word     dw 1000             ; 一个字，值为1000
    neg_word    dw -1               ; 有符号：0xFFFF = 65535(无符号) 或 -1(有符号)
    words_arr   dw 100, 200, 300   ; 3个字的数组
                ; 注意：x86是小端序！100在内存中是 64 00（低字节在前）

    ; ==================================================================
    ; DD - 定义双字(DWord)数据（4字节）
    ; ==================================================================

    my_dword    dd 100000           ; 一个双字
    float_val   dd 3.14             ; 32位浮点数（IEEE 754格式）
    address     dd 0x00401000       ; 存放一个地址（指针）
    dword_arr   dd 1, 2, 3, 4, 5   ; 5个双字的数组
                ; 每个元素占4字节，总共占20字节

    ; ==================================================================
    ; DQ - 定义四字(QWord)数据（8字节）
    ; ==================================================================

    my_qword    dq 1234567890123    ; 一个四字
    double_val  dq 3.14159265       ; 64位双精度浮点数

    ; ==================================================================
    ; 使用 equ 定义常量（不占用内存空间）
    ; ==================================================================

    ARRAY_SIZE  equ 10              ; 常量，编译时替换为10
    BUFFER_LEN  equ 256             ; 常量，编译时替换为256
    NULL        equ 0               ; 常量0
    NEWLINE     equ 0x0A            ; 常量换行符

; section .bss 定义未初始化数据（只预留空间，不占exe文件大小）
section .bss
    ; resb/resw/resd = Reserve Byte/Word/Dword
    buffer      resb 256            ; 预留256字节的空间
    big_array   resd 100            ; 预留100个双字（400字节）的空间
    temp_var    resd 1              ; 预留1个双字的临时变量

section .text
    extern _ExitProcess@4
    global _start

_start:
    ; ==================================================================
    ; 访问内存中的数据
    ; ==================================================================
    ; 用方括号 [] 表示"取这个地址的内容"

    mov     al, [my_byte]       ; 把 my_byte 地址处的1字节内容放入AL
                                ; [my_byte] = "打开my_byte标签对应的格子，拿出里面的东西"

    mov     ax, [my_word]       ; 把 my_word 地址处的2字节内容放入AX

    mov     eax, [my_dword]     ; 把 my_dword 地址处的4字节内容放入EAX

    ; ==================================================================
    ; 把数据写入内存
    ; ==================================================================

    mov     byte [buffer], 'X'      ; 写入1字节
                                    ; byte 是大小修饰符，告诉CPU操作1字节
    mov     word [buffer+2], 1000   ; 写入2字节，偏移2字节
    mov     dword [buffer+4], 99999 ; 写入4字节，偏移4字节

    ; ==================================================================
    ; 数组访问
    ; ==================================================================
    ; 数组元素地址 = 基地址 + 索引 * 元素大小

    ; 访问 dword_arr 的第3个元素（索引从0开始，所以是索引2）
    mov     eax, [dword_arr + 2*4]  ; dword_arr[2] = 3
                                    ; 每个元素4字节，所以偏移 = 2*4 = 8

    ; 用寄存器作为索引
    mov     ecx, 3                  ; 索引 = 3
    mov     eax, [dword_arr + ecx*4] ; dword_arr[3] = 4
                                    ; 偏移 = 3*4 = 12

    ; ==================================================================
    ; LEA 指令 - 加载有效地址（不访问内存！）
    ; ==================================================================

    lea     eax, [my_dword]     ; 把 my_dword 的地址放入EAX（不是内容！）
                                ; 与 mov eax, my_dword 效果相同
                                ; 但LEA更灵活，可以做地址计算

    lea     eax, [dword_arr + ecx*4] ; 计算 dword_arr[3] 的地址
                                    ; LEA不访问内存，只做数学运算！

    ; ==================================================================
    ; 地址计算演示
    ; ==================================================================
    ; LEA可以做高效的数学运算（这是个有趣的技巧）

    mov     ecx, 5
    lea     eax, [ecx + ecx*4]  ; eax = ecx * 5 = 25
                                ; 因为 x + 4x = 5x

    lea     eax, [ecx*8]       ; eax = ecx * 8 = 40
    lea     eax, [ecx*4 + 3]   ; eax = ecx * 4 + 3 = 23

    ; 退出程序
    push    0
    call    _ExitProcess@4

