; 第01课：寄存器详解
; CPU的"工作台"——通用寄存器、段寄存器、标志寄存器
;
; ============================================================================
; 一、什么是寄存器？
; ============================================================================
;
; 寄存器(Register)是CPU内部的高速存储单元，是CPU的"工作台"。
;
; 生活类比：
;   想象你在做手工活：
;   - 内存 = 仓库（大但远，取东西慢）
;   - 寄存器 = 工作台上的格子（小但近，取东西极快）
;   - CPU = 你本人（工人）
;
;   你不可能每次都跑去仓库拿材料，所以你把常用的材料放在工作台上。
;   CPU也是这样——它把当前正在处理的数据放在寄存器里。
;
; 寄存器的特点：
;   1. 速度极快（比内存快100倍以上）
;   2. 数量很少（x86只有十几个通用寄存器）
;   3. 大小固定（32位CPU的寄存器是32位 = 4字节）
;
; ============================================================================
; 二、通用寄存器（General Purpose Registers）
; ============================================================================
;
; x86有8个通用寄存器，它们的名字都有历史渊源：
;
; ┌──────────┬──────────────────────────────────────────────┐
; │ 寄存器   │ 用途                                        │
; ├──────────┼──────────────────────────────────────────────┤
; │ EAX      │ 累加器(Accumulator) - 算术运算、函数返回值  │
; │ EBX      │ 基址寄存器(Base) - 内存寻址基址            │
; │ ECX      │ 计数器(Counter) - 循环计数、移位次数       │
; │ EDX      │ 数据寄存器(Data) - I/O操作、乘除法辅助     │
; │ ESI      │ 源索引(Source Index) - 字符串操作源地址    │
; │ EDI      │ 目标索引(Destination Index) - 字符串目标   │
; │ ESP      │ 栈指针(Stack Pointer) - 指向栈顶          │
; │ EBP      │ 基址指针(Base Pointer) - 栈帧基址         │
; └──────────┴──────────────────────────────────────────────┘
;
; 注意 "E" 前缀 = Extended（扩展），因为32位是从16位扩展来的：
;   EAX = Extended AX  （32位）
;   AX  = 累加器       （16位）
;   AL  = AX的低8位    （8位）
;   AH  = AX的高8位    （8位）
;
;   ┌────────────────────────────────────┐
;   │              EAX (32位)             │
;   ├──────────────────────┬─────────────┤
;   │      (高16位)        │    AX (16位) │
;   │                      ├──────┬──────┤
;   │                      │ AH   │ AL   │
;   │                      │ 8位  │ 8位  │
;   └──────────────────────┴──────┴──────┘
;
; 生活类比：
;   EAX就像一个有4层的收纳盒：
;   - 最大的盒子叫EAX（能放32个零件）
;   - 下面两层叫AX（能放16个零件）
;   - AX又分上下两层：AH（高8个）和 AL（低8个）
;   - 你可以单独操作任何一层，但大的包含小的
;
; --------------------------------------------------------------------------
; 代码演示：通用寄存器的基本操作
; --------------------------------------------------------------------------

section .data
    msg_eax     db 'EAX = ', 0
    newline     db 0x0A, 0

section .text
    extern _ExitProcess@4

    global _start

_start:
    ; ==================================================================
    ; EAX - 累加器，最常用的寄存器
    ; ==================================================================
    ; 几乎所有算术运算都涉及EAX，函数返回值也放在EAX里

    mov     eax, 42             ; 把立即数42放入EAX
                                ; mov = move（传送），虽然叫"移动"但实际上是"复制"
                                ; 生活类比：把42这个数字写在EAX这个格子上

    mov     eax, 0x2A           ; 用十六进制表示同一个数（0x2A = 42）

    ; 操作EAX的子部分：
    mov     ax, 1000            ; 只修改AX（低16位），高16位不变
    mov     al, 55              ; 只修改AL（低8位），其他位不变
    mov     ah, 3               ; 只修改AH（次低8位），其他位不变

    ; ==================================================================
    ; EBX - 基址寄存器
    ; ==================================================================
    ; 常用于内存寻址的基址，或者作为临时存储

    mov     ebx, 0x1000         ; 把地址0x1000放入EBX
                                ; 后面可以用 [ebx] 来访问这个地址的内容

    ; ==================================================================
    ; ECX - 计数器
    ; ==================================================================
    ; 循环指令LOOP会自动使用ECX作为计数器
    ; 很多字符串操作也用ECX

    mov     ecx, 10             ; 设置循环计数为10
                                ; 当LOOP指令执行时，ECX会自动减1
                                ; 当ECX=0时，循环结束

    ; ==================================================================
    ; EDX - 数据寄存器
    ; ==================================================================
    ; 常与EAX配合使用：
    ;   - 乘法：EDX:EAX = EAX * 操作数（EDX存高32位，EAX存低32位）
    ;   - 除法：EAX = EDX:EAX / 操作数 的商
    ;          EDX = EDX:EAX / 操作数 的余数

    mov     eax, 100            ; 被除数低32位
    xor     edx, edx            ; 清零EDX（被除数高32位 = 0）
                                ; xor edx, edx 是清零的惯用写法
                                ; 比 mov edx, 0 更高效（机器码更短）
    mov     ecx, 7              ; 除数
    div     ecx                 ; EDX:EAX / ECX
                                ; 商 -> EAX = 14
                                ; 余数 -> EDX = 2

    ; ==================================================================
    ; ESI 和 EDI - 字符串操作寄存器
    ; ==================================================================
    ; ESI = Source Index（源索引）      -> 指向源数据
    ; EDI = Destination Index（目标索引）-> 指向目标位置
    ; 就像复制粘贴：ESI是"复制"的位置，EDI是"粘贴"的位置

    ; （字符串操作的详细用法将在后面的课程中讲解）

    ; ==================================================================
    ; ESP 和 EBP - 栈相关寄存器
    ; ==================================================================
    ; 这两个寄存器非常重要，将在"栈与函数调用"课程中详细讲解
    ;
    ; ESP = Stack Pointer（栈指针）
    ;   - 永远指向栈顶
    ;   - push 操作会让 ESP 减小（栈向下增长）
    ;   - pop 操作会让 ESP 增大
    ;
    ; EBP = Base Pointer（基址指针）
    ;   - 指向当前函数栈帧的起始位置
    ;   - 用于访问函数的局部变量和参数
    ;
    ; 生活类比：
    ;   栈就像一摞盘子：
    ;   - ESP 指向最上面那个盘子（栈顶）
    ;   - EBP 标记了这摞盘子的底部（当前函数的起点）
    ;   - push = 往上面放一个盘子（ESP往上移）
    ;   - pop  = 从上面拿走一个盘子（ESP往下移）

    ; 示例：观察ESP的变化
    mov     eax, 0xAAAA         ; 准备一些数据
    mov     ebx, 0xBBBB

    push    eax                 ; ESP = ESP - 4，然后把EAX的值存到[ESP]
                                ; 盘子上多了一个写着0xAAAA的盘子
    push    ebx                 ; ESP = ESP - 4，然后把EBX的值存到[ESP]
                                ; 又多了一个写着0xBBBB的盘子

    pop     ecx                 ; 把栈顶的值（0xBBBB）放入ECX，然后 ESP = ESP + 4
                                ; 拿走最上面的盘子
    pop     edx                 ; 把栈顶的值（0xAAAA）放入EDX，然后 ESP = ESP + 4
                                ; 拿走下面的盘子
    ; 注意：pop的顺序和push相反（后进先出 = LIFO）

    ; 退出程序
    push    0
    call    _ExitProcess@4

; ============================================================================
; 三、段寄存器（Segment Registers）
; ============================================================================
;
; 在32位保护模式下，段寄存器的作用被大大简化了：
;
;   CS - 代码段(Code Segment)   -> 指向当前代码所在的段
;   DS - 数据段(Data Segment)   -> 指向当前数据所在的段
;   SS - 栈段(Stack Segment)    -> 指向栈所在的段
;   ES - 附加段(Extra Segment)  -> 字符串操作的目标段
;   FS - 附加段2               -> Windows用来指向线程环境块(TEB)
;   GS - 附加段3               -> 备用
;
; 在Windows 32位程序中，通常不需要手动设置段寄存器。
; 但要知道FS的特殊用途：FS:[0] 指向 SEH（结构化异常处理）链。
;
; ============================================================================
; 四、标志寄存器（EFLAGS）
; ============================================================================
;
; EFLAGS是一个32位的寄存器，每一位都有特殊含义：
;
; 位   名称   含义                           生活类比
; ---  ----   ----                           ----
; 0    CF     进位标志(Carry Flag)           算术进位（如9+1=10，写0进1）
; 2    PF     奇偶标志(Parity Flag)          结果中1的个数是否为偶数
; 6    ZF     零标志(Zero Flag)              结果是否为0（是0就亮灯）
; 7    SF     符号标志(Sign Flag)            结果是否为负数（最高位=1）
; 11   OF     溢出标志(Overflow Flag)        有符号运算是否溢出
; 10   DF     方向标志(Direction Flag)       字符串操作方向（增/减）
;
; 生活类比：
;   想象一台计算器，上面有几个小灯：
;   - 绿灯(ZF)：如果计算结果是0，绿灯亮
;   - 红灯(SF)：如果结果是负数，红灯亮
;   - 黄灯(CF)：如果有进位/借位，黄灯亮
;   - 蓝灯(OF)：如果数字太大溢出了，蓝灯亮
;
;   CPU每做一次运算，这些灯就会自动更新！
;   后面的条件跳转指令就是根据这些灯来决定跳不跳的。

; --------------------------------------------------------------------------
; 标志位演示（独立参考代码，需要调试器单步观察）
; 注意：此段代码不参与主程序执行流程，仅供学习标志位变化使用
; --------------------------------------------------------------------------

_start_flags:
    ; 零标志 ZF 演示
    mov     eax, 5
    sub     eax, 5              ; 5 - 5 = 0，ZF = 1（置位）
                                ; 此时如果执行 JZ（Jump if Zero），就会跳转

    mov     eax, 5
    sub     eax, 3              ; 5 - 3 = 2，ZF = 0（清除）
                                ; 结果不是零，所以ZF不亮

    ; 进位标志 CF 演示
    mov     al, 255             ; AL = 0xFF（8位最大值）
    add     al, 1               ; 255 + 1 = 256，但8位装不下！
                                ; AL = 0，CF = 1（有进位）
                                ; 就像里程表999999归零一样

    ; 溢出标志 OF 演示
    mov     al, 127             ; AL = 0x7F（有符号8位最大正数）
    add     al, 1               ; 127 + 1 = 128？不对！
                                ; 有符号解读：0x80 = -128（溢出了！）
                                ; OF = 1
                                ;
                                ; 类比：温度计从127度突然跳到-128度

    ; 符号标志 SF 演示
    mov     eax, 3
    sub     eax, 5              ; 3 - 5 = -2
                                ; SF = 1（结果为负）
                                ; EAX = 0xFFFFFFFE（-2的补码表示）

    ; 退出
    push    0
    call    _ExitProcess@4

; ============================================================================
; 五、寄存器的使用约定
; ============================================================================
;
; 虽然通用寄存器理论上可以随便用，但有一些约定需要遵守：
;
; 1. EAX、ECX、EDX 是"调用者保存"寄存器(caller-saved)
;    - 函数调用后可能被修改
;    - 如果你想保留它们的值，调用前要自己push保存
;
; 2. EBX、ESI、EDI、EBP 是"被调用者保存"寄存器(callee-saved)
;    - 函数如果要使用它们，必须先保存，返回前恢复
;    - 调用者可以放心它们不会被改变
;
; 3. ESP 是栈指针，绝对不能随便修改！
;    - 除非你非常清楚自己在做什么
;
; 4. EAX 通常用于存放函数返回值
;
; 生活类比：
;   EAX/ECX/EDX = 一次性手套，用完就扔（函数可能弄脏它们）
;   EBX/ESI/EDI/EBP = 私人工具，别人用完要洗干净还给你
;   ESP = 工厂的传送带，不能随便停！

; ============================================================================
; 练习题
; ============================================================================
;
; 练习1（概念题）：
;   请画出EAX寄存器的结构图，标注EAX、AX、AH、AL各自的位范围。
;
; 练习2（代码题）：
;   写一段代码，把 EAX 设为 0x12345678，然后：
;   a) 只把 AL 改为 0xAA，其他位不变
;   b) 只把 AH 改为 0xBB，其他位不变
;   c) 最终 EAX 应该等于多少？
;
; 练习3（概念题）：
;   以下操作后，各标志位是什么状态？
;   mov eax, 100
;   add eax, 28
;   （提示：100+28=128，想想有没有溢出？是否为零？是否为负？）
;
; 练习4（操作题）：
;   按照push和pop的规则，写出以下操作后ECX和EDX的值：
;   mov eax, 10
;   mov ebx, 20
;   push eax
;   push ebx
;   pop ecx
;   pop edx
;
; 练习5（思考题）：
;   为什么 `xor eax, eax` 比 `mov eax, 0` 更常用来清零寄存器？
;   （提示：从机器码大小和执行速度两个角度思考）
;
; ============================================================================
; 参考答案
; ============================================================================
;
; 答案1：
;   31                              15       7      0
;   ┌───────────────────────────────┬────────┬──────┐
;   │          EAX (32位)           │AX(16位)│      │
;   │                               ├────────┼──────┤
;   │                               │  AH    │ AL   │
;   │                               │ 8位    │ 8位  │
;   └───────────────────────────────┴────────┴──────┘
;   位范围：EAX[31:0], AX[15:0], AH[15:8], AL[7:0]
;
; 答案2：
;   mov eax, 0x12345678  ; EAX = 0x12345678
;   mov al, 0xAA         ; EAX = 0x123456AA
;   mov ah, 0xBB         ; EAX = 0x1234BBAA
;   最终 EAX = 0x1234BBAA
;
; 答案3：
;   100 + 28 = 128 = 0x80
;   ZF = 0（结果不为零）
;   SF = 1（0x80的最高位=1，有符号解读为-128）
;   OF = 1（两个正数相加得到负数，有符号溢出）
;   CF = 0（无进位）
;
; 答案4：
;   push eax (10), push ebx (20)
;   栈：[20](栈顶), [10](栈底)
;   pop ecx -> ecx = 20
;   pop edx -> edx = 10
;   后进先出！
;
; 答案5：
;   1. 机器码大小：xor eax,eax 只需要2字节，mov eax,0 需要5字节
;   2. 执行速度：xor是简单的逻辑运算，比mov立即数更快
;   3. xor还会清零标志位，一举两得
;   4. 现代CPU对xor有特殊优化（依赖消除）
;
; ============================================================================
; 教授的话
; ============================================================================
;
; 【核心收获】
;
;   1. 8个通用寄存器：EAX(累加)、EBX(基址)、ECX(计数)、EDX(数据)
;      ESI(源索引)、EDI(目标索引)、ESP(栈指针)、EBP(帧指针)
;   2. 32位可拆分：EAX -> AX -> AH/AL
;   3. EFLAGS：ZF(零)、CF(进位)、SF(符号)、OF(溢出)
;   4. 调用约定：EAX/ECX/EDX 调用者保存，EBX/ESI/EDI/EBP 被调用者保存
;   5. xor eax, eax 比 mov eax, 0 更短更快（清零惯用写法）
;
; 【常见陷阱】
;
;   1. ESP 是栈指针，绝不能随意修改，否则程序崩溃
;   2. 函数调用后 EAX/ECX/EDX 可能被修改，需要提前 push 保存
;   3. 操作 AL 不影响高24位，但操作 AX 会影响高16位
;   4. 标志位是"自动挡"——每条算术指令都会更新，不需要手动设置
;
