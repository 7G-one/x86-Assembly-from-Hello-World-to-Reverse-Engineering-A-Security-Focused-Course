; 一、CMP 指令 - 比较
; ============================================================================
;
; 语法：CMP 操作数1, 操作数2
; 作用：计算 操作数1 - 操作数2，只更新标志位，不保存结果
;
; 生活类比：
;   CMP就像天平：你放两个东西上去比较，看看哪个重，
;   但你不会真的把它们混合在一起——你只是看天平的指针偏向哪边。
;
; CMP后的标志位状态：
;   操作数1 = 操作数2  ->  ZF=1（相等）
;   操作数1 > 操作数2  ->  ZF=0, SF=OF（大于）
;   操作数1 < 操作数2  ->  ZF=0, SF!=OF（小于）
;
; 无符号比较看 CF：
;   操作数1 > 操作数2  ->  CF=0
;   操作数1 < 操作数2  ->  CF=1

section .data
    msg_yes db 'Yes!', 0x0A, 0
    msg_no  db 'No!', 0x0A, 0
    val_a   dd 10
    val_b   dd 20

section .text
    extern _ExitProcess@4
    global _start

_start:
    ; ==================================================================
    ; CMP 基本用法
    ; ==================================================================

    mov     eax, 10
    cmp     eax, 10             ; 10 - 10 = 0 -> ZF=1
    ; 此时 JZ/JE 可以跳转（因为相等）

    mov     eax, 20
    cmp     eax, 10             ; 20 - 10 = 10 -> ZF=0, SF=0
    ; 此时 JG 可以跳转（因为20 > 10，有符号大于）

    mov     eax, 5
    cmp     eax, 10             ; 5 - 10 = -5 -> ZF=0, SF=1
    ; 此时 JL 可以跳转（因为5 < 10，有符号小于）

    ; ==================================================================
    ; 无条件跳转 JMP
    ; ==================================================================
    ; JMP 标签  =>  无条件跳转到标签处
    ; 就像 goto，没有任何条件，直接跳

    jmp     .skip_this          ; 跳过下面的代码
    ; 这行代码永远不会执行
    mov     eax, 999
.skip_this:
    ; 跳转到这里继续执行

    ; ==================================================================
    ; 条件跳转指令
    ; ==================================================================
    ;
    ; 条件跳转根据标志位决定是否跳转。
    ; 通常跟在 CMP 或 TEST 之后使用。
    ;
    ; ┌─────────────────────────────────────────────────────┐
    ; │            条件跳转指令速查表                       │
    ; ├──────────┬────────────┬─────────────────────────────┤
    ; │ 指令     │ 条件       │ 含义                        │
    ; ├──────────┼────────────┼─────────────────────────────┤
    ; │ JE/JZ    │ ZF=1       │ 等于 / 为零                 │
    ; │ JNE/JNZ  │ ZF=0       │ 不等于 / 不为零             │
    ; │ JG/JNLE  │ ZF=0且SF=OF│ 有符号大于                  │
    ; │ JGE/JNL  │ SF=OF      │ 有符号大于等于              │
    ; │ JL/JNGE  │ SF!=OF     │ 有符号小于                  │
    ; │ JLE/JNG  │ ZF=1或SF!=OF│ 有符号小于等于             │
    ; │ JA/JNBE  │ CF=0且ZF=0 │ 无符号大于                  │
    ; │ JAE/JNB  │ CF=0       │ 无符号大于等于              │
    ; │ JB/JNAE  │ CF=1       │ 无符号小于                  │
    ; │ JBE/JNA  │ CF=1或ZF=1 │ 无符号小于等于              │
    ; │ JS       │ SF=1       │ 结果为负                    │
    ; │ JNS      │ SF=0       │ 结果为非负                  │
    ; │ JO       │ OF=1       │ 溢出                        │
    ; │ JNO      │ OF=0       │ 无溢出                      │
    ; └──────────┴────────────┴─────────────────────────────┘
    ;
    ; 记忆技巧：
    ;   J = Jump（跳转）
    ;   E = Equal（等于）
    ;   N = Not（不）
    ;   G = Greater（大于）
    ;   L = Less（小于）
    ;   A = Above（无符号大于）
    ;   B = Below（无符号小于）
    ;   S = Sign（符号）
    ;   O = Overflow（溢出）
    ;
    ; 生活类比：
    ;   CMP + JE  = 如果相等就跳（两个人体重一样就走左边）
    ;   CMP + JG  = 如果大于就跳（如果比我重就走右边）
    ;   JMP       = 不管怎样都跳（直接走）

    ; ==================================================================
    ; 示例1：简单的 if-else
    ; ==================================================================
    ; C语言：if (eax == 42) { ... } else { ... }

    mov     eax, 42
    cmp     eax, 42             ; 比较 EAX 和 42
    jne     .else_branch        ; 如果不相等，跳到 else
    ; if 分支：
    ; eax == 42 的情况
    mov     ebx, 1              ; 标记进入 if 分支
    jmp     .end_if             ; 跳过 else
.else_branch:
    ; else 分支：
    mov     ebx, 0              ; 标记进入 else 分支
.end_if:
    ; 继续执行

    ; ==================================================================
    ; 示例2：求两个数的最大值
    ; ==================================================================
    ; C语言：max = (a > b) ? a : b;

    mov     eax, 30             ; a = 30
    mov     ebx, 50             ; b = 50

    cmp     eax, ebx            ; 比较 a 和 b
    jge     .a_is_bigger        ; 如果 a >= b，跳转
    ; b 更大：
    mov     ecx, ebx            ; max = b
    jmp     .done_max
.a_is_bigger:
    ; a 更大或相等：
    mov     ecx, eax            ; max = a
.done_max:
    ; ECX = max(30, 50) = 50

    ; ==================================================================
    ; 示例3：绝对值
    ; ==================================================================
    ; C语言：abs_val = (x < 0) ? -x : x;

    mov     eax, -42
    test    eax, eax            ; 检查 EAX 的符号
    jns     .is_positive        ; 如果非负（SF=0），跳转
    neg     eax                 ; 如果负数，取反
.is_positive:
    ; EAX = 42（绝对值）

    ; ==================================================================
    ; 示例4：多分支（switch-case）
    ; ==================================================================
    ; C语言：
    ; switch (eax) {
    ;   case 1: ... break;
    ;   case 2: ... break;
    ;   case 3: ... break;
    ;   default: ...
    ; }

    mov     eax, 2              ; 测试值

    cmp     eax, 1
    je      .case_1
    cmp     eax, 2
    je      .case_2
    cmp     eax, 3
    je      .case_3
    jmp     .default            ; 都不匹配，跳到 default

.case_1:
    mov     ebx, 100
    jmp     .end_switch
.case_2:
    mov     ebx, 200
    jmp     .end_switch
.case_3:
    mov     ebx, 300
    jmp     .end_switch
.default:
    mov     ebx, 0
.end_switch:
    ; EBX = 200（因为 eax=2）

    ; ==================================================================
    ; 示例5：for循环
    ; ==================================================================
    ; C语言：for (int i=0; i<10; i++) { sum += i; }

    xor     eax, eax            ; sum = 0
    xor     ecx, ecx            ; i = 0
.for_loop:
    cmp     ecx, 10             ; i < 10?
    jge     .for_end            ; 如果 i >= 10，结束循环
    add     eax, ecx            ; sum += i
    inc     ecx                 ; i++
    jmp     .for_loop           ; 回到循环开始
.for_end:
    ; EAX = 0+1+2+...+9 = 45

    ; ==================================================================
    ; 示例6：while循环
    ; ==================================================================
    ; C语言：while (eax > 0) { eax--; }

    mov     eax, 5
.while_loop:
    test    eax, eax            ; eax > 0?
    jle     .while_end          ; 如果 eax <= 0，结束
    dec     eax                 ; eax--
    jmp     .while_loop
.while_end:
    ; EAX = 0

    ; ==================================================================
    ; 示例7：嵌套条件
    ; ==================================================================
    ; C语言：
    ; if (a > 0 && b > 0) { result = 1; }
    ; 短路求值：如果 a <= 0，就不检查 b

    mov     eax, 5              ; a = 5
    mov     ebx, 10             ; b = 10
    xor     ecx, ecx            ; result = 0

    test    eax, eax            ; a > 0?
    jle     .nested_end         ; 如果 a <= 0，跳过
    test    ebx, ebx            ; b > 0?
    jle     .nested_end         ; 如果 b <= 0，跳过
    ; 两个条件都满足：
    mov     ecx, 1              ; result = 1
.nested_end:

    ; ==================================================================
    ; 实用示例：字符串比较（独立参考代码）
    ; 注意：此段代码不参与主程序执行流程，仅供学习字符串比较思路
    ; ==================================================================

    ; 比较两个字符串是否相等（简化版）
section .data
    str1    db 'Hello', 0
    str2    db 'Hello', 0
    str3    db 'World', 0

section .text
_start_strcmp:
    ; 比较 str1 和 str2
    lea     esi, [str1]         ; ESI -> str1
    lea     edi, [str2]         ; EDI -> str2
    xor     ecx, ecx            ; 索引 = 0

.cmp_loop:
    mov     al, [esi + ecx]     ; 取 str1[i]
    cmp     al, [edi + ecx]     ; 和 str2[i] 比较
    jne     .not_equal          ; 不相等就跳出
    test    al, al              ; 检查是否到字符串结尾（'\0'）
    jz      .is_equal           ; 到结尾了，所有字符都相等
    inc     ecx                 ; 下一个字符
    jmp     .cmp_loop

.is_equal:
    mov     eax, 1              ; 标记：相等
    jmp     .strcmp_done
.not_equal:
    mov     eax, 0              ; 标记：不相等
.strcmp_done:

    ; 退出程序
    push    0
    call    _ExitProcess@4

