; 练习题
; ============================================================================
;
; 练习1（代码题）：
;   写一段代码，判断 EAX 中的数是正数、负数还是零，
;   分别把 1、-1、0 存入 EBX。
;
; 练习2（代码题）：
;   写一段代码，计算 1 到 N 的和（N 存在 ECX 中），
;   使用 for 循环实现，结果放在 EAX 中。
;
; 练习3（概念题）：
;   JE 和 JA 有什么区别？分别在什么情况下使用？
;
; 练习4（代码题）：
;   写一段代码，实现以下 C 代码：
;   if (x > 100) y = 1;
;   else if (x > 50) y = 2;
;   else if (x > 0) y = 3;
;   else y = 0;
;
; 练习5（思考题）：
;   为什么比较有符号数用 JG/JL，比较无符号数用 JA/JB？
;   给出一个具体的例子说明混用会出错。
;
; ============================================================================
; 参考答案
; ============================================================================
;
; 答案1：
;   test eax, eax
;   jz   .is_zero
;   jns  .is_positive
;   mov  ebx, -1           ; 负数
;   jmp  .done
;   .is_positive:
;   mov  ebx, 1            ; 正数
;   jmp  .done
;   .is_zero:
;   mov  ebx, 0            ; 零
;   .done:
;
; 答案2：
;   xor eax, eax            ; sum = 0
;   mov edx, 1              ; i = 1
;   .loop:
;       add eax, edx        ; sum += i
;       inc edx             ; i++
;       cmp edx, ecx        ; i <= N?
;       jle .loop
;
; 答案3：
;   JE（Jump if Equal）：ZF=1时跳转，用于相等比较
;   JA（Jump if Above）：CF=0且ZF=0时跳转，用于无符号大于比较
;   JE用于 == 操作，JA用于 > 操作（无符号）
;   不能混用：JE 检查相等，JA 检查大于
;
; 答案4：
;   cmp eax, 100
;   jle .not_100
;   mov ebx, 1
;   jmp .done
;   .not_100:
;   cmp eax, 50
;   jle .not_50
;   mov ebx, 2
;   jmp .done
;   .not_50:
;   test eax, eax
;   jle .not_pos
;   mov ebx, 3
;   jmp .done
;   .not_pos:
;   mov ebx, 0
;   .done:
;
; 答案5：
;   有符号数和无符号数的比较逻辑不同：
;   0xFFFFFFFF 作为无符号是 4294967295（很大）
;   0xFFFFFFFF 作为有符号是 -1（很小）
;   用 JA 比较：0xFFFFFFFF > 0x00000001（无符号：4294967295 > 1，正确）
;   用 JG 比较：0xFFFFFFFF < 0x00000001（有符号：-1 < 1，正确）
;   如果混用，-1 会被当成 4294967295 来比较，结果就错了
;
; ============================================================================
; 教授的话
; ============================================================================
;
; 【核心收获】
;
;   1. CMP 做减法但不保存结果，只更新标志位（天平比较，不混合）
;   2. JMP 无条件跳转，条件跳转根据 EFLAGS 决定是否跳
;   3. 有符号比较：JG(大于)/JL(小于)/JGE/JLE
;   4. 无符号比较：JA(高于)/JB(低于)/JAE/JBE
;   5. CMP + 条件跳转 = if-else、switch-case、循环等所有控制结构
;
; 【常见陷阱】
;
;   1. 有符号和无符号比较不能混用！0xFFFFFFFF 有符号是-1，无符号是4294967295
;   2. JE(相等) 和 JA(无符号大于) 不能混用
;   3. 跳转标签必须在同一段内，且距离不能超过短跳转范围(128字节)
;   4. 条件跳转只看标志位，忘记 CMP 就跳转会用到上次的标志位
;
