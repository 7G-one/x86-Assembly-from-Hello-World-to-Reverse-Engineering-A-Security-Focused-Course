; 一、什么是逆向分析？
; ============================================================================
;
; 逆向分析(Reverse Engineering)是从已编译的程序中还原出程序逻辑的过程。
;
; 生活类比：
;   想象你拿到一个黑盒子（编译后的程序），你能看到输入和输出，
;   但看不到里面的电路。逆向分析就是拆开盒子，研究里面的电路。
;
; 逆向分析的应用：
;   1. 恶意软件分析 - 研究病毒的工作原理
;   2. 漏洞挖掘 - 发现软件中的安全漏洞
;   3. CTF比赛 - Reverse和Pwn题目
;   4. 软件破解 - 绕过授权验证（合法的安全研究）
;   5. 互操作性 - 理解私有协议/格式
;
; ============================================================================
; 二、逆向工具
; ============================================================================
;
; 常用逆向工具：
;
; 1. IDA Pro - 最强大的反汇编器
;    - 支持多种架构
;    - 自动生成函数调用图
;    - 交互式分析
;    - 有免费版本（IDA Free）
;
; 2. Ghidra - NSA开源的逆向工具
;    - 免费，功能强大
;    - 有反编译器
;    - 支持多种架构
;
; 3. x64dbg - 调试器
;    - 动态调试
;    - 断点、单步执行
;    - 内存查看
;
; 4. OllyDbg - 经典调试器（32位）
;
; 5. Radare2 - 命令行逆向工具
;
; ============================================================================
; 三、IDA使用基础
; ============================================================================
;
; IDA的基本操作：
;   1. 打开文件：拖拽exe到IDA窗口
;   2. 分析：IDA会自动分析代码
;   3. 视图：
;      - IDA View：反汇编视图
;      - Hex View：十六进制视图
;      - Strings窗口：查找字符串
;      - Functions窗口：函数列表
;
; IDA快捷键：
;   空格    - 在图形视图和文本视图之间切换
;   G       - 跳转到指定地址
;   X       - 查看交叉引用
;   N       - 重命名
;   F5      - 反编译（伪代码）
;   ;       - 添加注释
;
; 生活类比：
;   IDA就像一个X光机，能看透程序的内部结构。
;   函数就是器官，变量就是血管，代码就是神经。

; ============================================================================
; 四、识别常见代码模式
; ============================================================================
;
; 通过学习汇编，你能识别以下常见模式：
;
; 1. 函数序言/尾声
;    push ebp
;    mov ebp, esp
;    sub esp, N        ; 函数开始
;    ...
;    mov esp, ebp
;    pop ebp
;    ret               ; 函数结束
;
; 2. if-else
;    cmp eax, ebx
;    jne .else
;    ; if 分支
;    jmp .end
;    .else:
;    ; else 分支
;    .end:
;
; 3. for循环
;    xor ecx, ecx      ; i = 0
;    .loop:
;    cmp ecx, 10       ; i < 10?
;    jge .end
;    ; 循环体
;    inc ecx
;    jmp .loop
;    .end:
;
; 4. 函数调用
;    push arg3
;    push arg2
;    push arg1
;    call function
;    add esp, 12       ; 清理参数
;
; 5. switch-case
;    cmp eax, 1
;    je .case1
;    cmp eax, 2
;    je .case2
;    jmp .default

section .data
    ; 逆向分析的目标程序示例
    secret_msg  db 'Congratulations! You found the secret!', 0x0A, 0
    fail_msg    db 'Wrong answer!', 0x0A, 0
    password    db 'CTF{assembly_is_fun}', 0

section .text
    extern _ExitProcess@4
    global _start

_start:
    ; ==================================================================
    ; 一个简单的"验证程序"示例
    ; ==================================================================
    ; 这个程序检查输入是否等于密码
    ; 逆向分析的目标：找到密码是什么

    ; 模拟输入（实际中从用户读取）
    lea     esi, [password]     ; 正确的密码
    lea     edi, [user_input]   ; 用户输入（假设已读取）

    ; 逐字节比较
    xor     ecx, ecx            ; 索引 = 0
.check_loop:
    mov     al, [esi + ecx]     ; 取密码字符
    cmp     al, [edi + ecx]     ; 和输入比较
    jne     .wrong              ; 不相等，密码错误
    test    al, al              ; 是否到结尾？
    jz      .correct            ; 到结尾了，所有字符都匹配
    inc     ecx
    jmp     .check_loop

.correct:
    ; 密码正确
    ; 输出成功消息...
    jmp     .done

.wrong:
    ; 密码错误
    ; 输出失败消息...

.done:
    push    0
    call    _ExitProcess@4

; 用户输入缓冲区
section .bss
    user_input  resb 64

; ============================================================================
; 五、识别加密算法
; ============================================================================
;
; 常见的简单加密算法在汇编中的特征：
;
; 1. XOR加密
;    .loop:
;    xor byte [esi], key    ; 特征：XOR操作
;    inc esi
;    loop .loop
;
; 2. Caesar密码（位移）
;    .loop:
;    add byte [esi], N      ; 特征：ADD/SUB固定值
;    ; 或 sub byte [esi], N
;    inc esi
;    loop .loop
;
; 3. Base64编码
;    特征：大量的位操作（SHR、SHL、AND）
;    和一个64字符的查找表
;
; 4. AES/DES
;    特征：大量的S盒查找、轮函数、密钥扩展

; XOR加密解密函数
; void xor_encrypt(char *data, int len, char key) {
;     for (int i = 0; i < len; i++)
;         data[i] ^= key;
; }
xor_encrypt:
    push    ebp
    mov     ebp, esp
    push    esi

    mov     esi, [ebp + 8]      ; data指针
    mov     ecx, [ebp + 12]     ; 长度
    mov     al, [ebp + 16]      ; 密钥

.loop:
    test    ecx, ecx
    jz      .done
    xor     byte [esi], al      ; 加密一个字节
    inc     esi
    dec     ecx
    jmp     .loop

.done:
    pop     esi
    pop     ebp
    ret

