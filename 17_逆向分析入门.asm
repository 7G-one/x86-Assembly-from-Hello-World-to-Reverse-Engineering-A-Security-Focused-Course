; 六、反调试技术简介
; ============================================================================
;
; 恶意软件经常使用反调试技术来对抗分析：
;
; 1. IsDebuggerPresent
;    检查PEB->BeingDebugged字段
;    mov eax, [fs:0x30]      ; PEB
;    cmp byte [eax+2], 0     ; BeingDebugged
;    jne .debugger_detected
;
; 2. 时间检查
;    执行前后比较时间，调试时会更慢
;    rdtsc
;    ; ... 代码 ...
;    rdtsc
;    ; 比较两次的时间差
;
; 3. INT 2D
;    专用的调试中断
;
; 4. 自修改代码
;    运行时修改自己的代码，干扰静态分析

; ============================================================================
; 七、实战逆向小例子
; ============================================================================
;
; 给你一个"谜题"程序，尝试逆向分析找到正确的输入：
;
; 以下函数检查输入是否正确：
; check_input:
;     push ebp
;     mov ebp, esp
;     mov esi, [ebp+8]       ; 输入字符串
;     xor ecx, ecx
;     xor eax, eax
;
; .loop:
;     mov bl, [esi + ecx]
;     test bl, bl
;     jz .check
;     xor bl, 0x37           ; XOR 0x37
;     add eax, ebx            ; 累加
;     inc ecx
;     jmp .loop
;
; .check:
;     cmp eax, 0x1A4          ; 和是否等于 0x1A4?
;     je .correct
;     xor eax, eax            ; 返回0（错误）
;     jmp .done
; .correct:
;     mov eax, 1              ; 返回1（正确）
; .done:
;     pop ebp
;     ret
;
; 分析：
;   1. 函数接收一个字符串
;   2. 对每个字符 XOR 0x37
;   3. 把XOR后的值累加
;   4. 检查累加和是否等于 0x1A4
;
; 要找到正确的输入，需要逆向这个计算过程。

