;
; 答案2：
;   int sum = 0;
;   for (int i = 0; i < 100; i++) {
;       sum += arr[i];
;   }
;   这是一个数组求和的for循环。
;
; 答案3：
;   累加和需要等于 0x1A4 = 420
;   每个字符先 XOR 0x37 再累加
;   假设输入 "AB"：
;   'A' = 0x41, XOR 0x37 = 0x76 = 118
;   'B' = 0x42, XOR 0x37 = 0x75 = 117
;   和 = 118 + 117 = 235 ≠ 420
;   需要更多字符或其他组合
;   可以用Python脚本暴力搜索
