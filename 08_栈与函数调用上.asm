; 练习题
; ============================================================================
;
; 练习1（概念题）：
;   描述 CALL 指令执行时栈的变化过程（画出栈的变化图）。
;
; 练习2（代码题）：
;   写一个函数 multiply(a, b)，使用 cdecl 约定，返回 a * b。
;   然后从主程序调用它。
;
; 练习3（概念题）：
;   cdecl 和 stdcall 有什么区别？各自的优缺点是什么？
;
; 练习4（代码题）：
;   写一个函数 max3(a, b, c)，返回三个数中的最大值。
;
; 练习5（思考题）：
;   为什么函数返回前必须确保栈是平衡的（ESP回到调用前的值）？
;   如果栈不平衡会怎样？
;
; ============================================================================
; 参考答案
; ============================================================================
;
; 答案1：
;   CALL执行前：
;   栈顶 -> [局部变量/旧数据]
;
;   CALL执行后（跳转前）：
;   栈顶 -> [返回地址] <- ESP
;           [局部变量/旧数据]
;
;   函数中 PUSH EBP 后：
;   栈顶 -> [旧EBP] <- ESP
;           [返回地址]
;           [局部变量/旧数据]
;
; 答案2：
;   multiply:
;       mov eax, [esp+4]
;       imul eax, [esp+8]
;       ret
;   ; 调用：
;       push 6
;       push 7
;       call multiply
;       add esp, 8
;       ; EAX = 42
;
; 答案3：
;   cdecl：调用者清栈，支持变参函数（printf），函数名无修饰
;   stdcall：函数清栈，代码更紧凑，但不支持变参函数
;   Windows API用stdcall因为它是固定的参数数量
;
; 答案4：
;   max3:
;       mov eax, [esp+4]    ; a
;       mov ecx, [esp+8]    ; b
;       cmp eax, ecx
;       jge .a_bigger
;       mov eax, ecx        ; max = b
;   .a_bigger:
;       mov ecx, [esp+12]   ; c
;       cmp eax, ecx
;       jge .done
;       mov eax, ecx        ; max = c
;   .done:
;       ret
;
; 答案5：
;   如果栈不平衡，ESP不会回到正确位置。
;   后续的POP会弹出错误的数据，RET会跳转到错误的地址，
;   程序会崩溃或产生不可预测的行为。
;   栈是程序执行的基础，必须严格保持平衡。
;
; ============================================================================
; 教授的话
; ============================================================================
;
; 【核心收获】
;
;   1. 栈 = 后进先出(LIFO)，向低地址增长，ESP永远指向栈顶
;   2. CALL = push 返回地址 + jmp 到函数；RET = pop 返回地址 + jmp 回去
;   3. cdecl：调用者清栈（add esp, N），支持变参函数
;   4. stdcall：函数自己清栈（ret N），Windows API默认
;   5. 返回值放在 EAX，参数从右到左压栈
;   6. PUSHAD/POPAD 批量保存/恢复所有通用寄存器
;
; 【常见陷阱】
;
;   1. cdecl 调用后忘记 add esp, N，栈不平衡导致后续崩溃
;   2. stdcall 的函数用 ret N 返回，不要用普通 ret
;   3. 函数可能修改 EAX/ECX/EDX，需要提前 push 保存
;   4. 参数从右到左压栈，第一个参数在栈顶（最近push的）
