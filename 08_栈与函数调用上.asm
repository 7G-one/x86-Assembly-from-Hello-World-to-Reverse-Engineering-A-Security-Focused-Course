; 一、栈的工作原理
; ============================================================================
;
; 栈(Stack)是内存中一块特殊区域，遵循"后进先出"(LIFO)原则。
;
; 生活类比：
;   栈就像一摞盘子：
;   - 你只能从最上面放盘子（PUSH）
;   - 你只能从最上面拿盘子（POP）
;   - 最后放的盘子最先被拿走（后进先出）
;
;   ┌─────────┐
;   │  盘子3  │ <- ESP（栈顶指针，指向最上面的盘子）
;   ├─────────┤
;   │  盘子2  │
;   ├─────────┤
;   │  盘子1  │
;   └─────────┘
;   低地址在上，高地址在下（栈向下增长）
;
; 栈的关键特性：
;   1. 向低地址增长（push时ESP减小）
;   2. 后进先出（LIFO）
;   3. ESP永远指向栈顶
;   4. 栈溢出：push太多数据会超出栈空间
;
; x86栈操作指令：
;   PUSH 源    - 压栈：ESP -= 4，然后 [ESP] = 源
;   POP  目标  - 出栈：目标 = [ESP]，然后 ESP += 4
;   PUSHAD    - 保存所有通用寄存器
;   POPAD     - 恢复所有通用寄存器
;   PUSHFD    - 保存标志寄存器
;   POPFD     - 恢复标志寄存器

section .data
    msg1    db 'Function called!', 0x0A, 0
    msg2    db 'Returned!', 0x0A, 0

section .text
    extern _ExitProcess@4
    global _start

_start:
    ; ==================================================================
    ; 栈的基本操作
    ; ==================================================================

    mov     eax, 0xAAAAAAAA
    mov     ebx, 0xBBBBBBBB
    mov     ecx, 0xCCCCCCCC

    ; 压栈（注意顺序）
    push    eax                 ; 栈：[AAAAAAAA]
    push    ebx                 ; 栈：[BBBBBBBB][AAAAAAAA]
    push    ecx                 ; 栈：[CCCCCCCC][BBBBBBBB][AAAAAAAA]
    ; ESP每次减少4

    ; 出栈（顺序相反）
    pop     edx                 ; EDX = CCCCCCCC，栈：[BBBBBBBB][AAAAAAAA]
    pop     esi                 ; ESI = BBBBBBBB，栈：[AAAAAAAA]
    pop     edi                 ; EDI = AAAAAAAA，栈：空
    ; ESP每次增加4

    ; ==================================================================
    ; PUSHAD/POPAD - 批量保存/恢复寄存器
    ; ==================================================================

    pushad                      ; 保存所有8个通用寄存器到栈中
    ; 顺序：EAX, ECX, EDX, EBX, ESP, EBP, ESI, EDI
    ; （ESP的值是PUSHAD之前的值）

    ; ... 这里可以随意使用寄存器 ...
    mov     eax, 999
    mov     ebx, 888

    popad                       ; 恢复所有寄存器（EAX, EBX等恢复原值）

    ; ==================================================================
    ; CALL 和 RET - 函数调用
    ; ==================================================================
    ;
    ; CALL 标签：
    ;   1. 把下一条指令的地址（返回地址）压入栈
    ;   2. 跳转到标签处执行
    ;
    ; RET：
    ;   1. 从栈顶弹出返回地址
    ;   2. 跳转到该地址继续执行
    ;
    ; 生活类比：
    ;   CALL = 你打电话叫外卖，记住了你当前的位置（压栈返回地址）
    ;   RET  = 外卖送完后，回到你打电话的位置继续干活

    call    my_function         ; 调用函数
    ; 函数返回后，从这里继续执行

    mov     eax, 1              ; 这行在函数返回后执行

    ; 退出程序
    push    0
    call    _ExitProcess@4

; ============================================================================
; 自定义函数
; ============================================================================
my_function:
    ; 函数体
    mov     eax, 42             ; 做一些事情

    ret                         ; 返回到调用者
    ; ret 会弹出栈顶的返回地址，跳转到那里
    ; 相当于：pop eip（虽然不能直接操作EIP，但原理如此）

; ============================================================================
; 二、函数调用约定
; ============================================================================
;
; 调用约定(Calling Convention)规定了：
;   1. 参数怎么传递（从左到右？从右到左？用栈还是寄存器？）
;   2. 谁负责清理栈（调用者还是被调用者？）
;   3. 返回值放在哪里（通常是EAX）
;   4. 哪些寄存器需要保存
;
; 常见调用约定：
;
; ┌────────────┬────────────────────────────────────────────┐
; │ 约定       │ 特点                                       │
; ├────────────┼────────────────────────────────────────────┤
; │ cdecl      │ C语言默认，参数从右到左压栈，调用者清栈    │
; │ stdcall    │ Windows API默认，参数从右到左压栈，函数清栈│
; │ fastcall   │ 前两个参数用ECX/EDX，其余压栈              │
; │ thiscall   │ C++用，this指针用ECX                       │
; └────────────┴────────────────────────────────────────────┘
;
; Windows 32位常用 stdcall（函数名带 @N 后缀，N是参数字节数）
; Linux 32位常用 cdecl
;
; 生活类比：
;   调用约定就像餐厅的服务规范：
;   - cdecl：服务员上完菜（传参），客人自己收盘子（调用者清栈）
;   - stdcall：服务员上完菜，服务员收盘子（函数自己清栈）

; ============================================================================
; 示例：cdecl 调用约定
; ============================================================================
; int add(int a, int b) { return a + b; }
; 调用：result = add(3, 5);

add_cdecl:
    ; cdecl约定：参数在栈上，调用者清栈
    ; 函数开头，栈的状态：
    ; [ESP]   = 返回地址
    ; [ESP+4] = 参数 a (第一个参数)
    ; [ESP+8] = 参数 b (第二个参数)

    mov     eax, [esp + 4]      ; eax = a（第一个参数）
    add     eax, [esp + 8]      ; eax += b（第二个参数）
    ; 返回值在 EAX 中

    ret                         ; 返回（cdecl由调用者清栈）

_call_cdecl:
    ; 调用 add(3, 5)
    push    5                   ; 第二个参数先压栈（从右到左）
    push    3                   ; 第一个参数后压栈
    call    add_cdecl           ; 调用函数
    add     esp, 8              ; 调用者清理栈！（2个参数 * 4字节 = 8）
    ; EAX = 3 + 5 = 8

; ============================================================================
; 示例：stdcall 调用约定
; ============================================================================
; 参数从右到左压栈，函数负责清理栈

add_stdcall:
    ; stdcall约定：参数在栈上，函数清栈
    mov     eax, [esp + 4]      ; eax = a
    add     eax, [esp + 8]      ; eax += b

    ret     8                   ; 返回并清理8字节的参数！
    ; ret N = pop 返回地址 + ESP += N
    ; 相当于在ret之前执行了 add esp, 8

_call_stdcall:
    push    5                   ; 第二个参数
    push    3                   ; 第一个参数
    call    add_stdcall         ; 函数会自己清栈
    ; 不需要 add esp, 8（函数已经清理了）
    ; EAX = 8

; ============================================================================
; 示例：带更多参数的函数
; ============================================================================
; int calc(int a, int b, int c, int d) { return (a+b) * (c-d); }

calc_func:
    push    ebp                 ; 保存旧的EBP（后面课程详细讲）
    mov     ebp, esp            ; 建立栈帧

    mov     eax, [ebp + 8]      ; a（第一个参数）
    add     eax, [ebp + 12]     ; a + b
    mov     ecx, [ebp + 16]     ; c
    sub     ecx, [ebp + 20]     ; c - d
    imul    eax, ecx            ; (a+b) * (c-d)

    pop     ebp                 ; 恢复EBP
    ret                         ; 返回（cdecl由调用者清栈）

_call_calc:
    push    10                  ; d = 10（第四个参数，最后压栈）
    push    3                   ; c = 3
    push    5                   ; b = 5
    push    2                   ; a = 2
    call    calc_func           ; 调用
    add     esp, 16             ; 清理4个参数（4*4=16字节）
    ; EAX = (2+5) * (3-10) = 7 * (-7) = -49

