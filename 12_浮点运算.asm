; 练习题
; ============================================================================
;
; 练习1（代码题）：
;   用FPU计算 3.14 * 2.5 + 1.0 的结果。
;
; 练习2（代码题）：
;   写一个函数，接收两个float参数，返回它们的平均值。
;
; 练习3（概念题）：
;   FST 和 FSTP 有什么区别？为什么大多数情况下用 FSTP？
;
; 练习4（代码题）：
;   用FPU计算一个直角三角形的斜边长度（已知两条直角边）。
;   c = √(a² + b²)
;
; 练习5（思考题）：
;   浮点数为什么不能精确表示所有小数？（如 0.1 + 0.2 != 0.3）
;
; ============================================================================
; 参考答案
; ============================================================================
;
; 答案1：
;   fldpi                       ; ST(0) = 3.14159...
;   fmul dword [val_2_5]        ; ST(0) *= 2.5
;   fadd dword [val_1_0]        ; ST(0) += 1.0
;   fstp dword [result]
;
; 答案2：
;   float_avg:
;       push ebp
;       mov ebp, esp
;       fld dword [ebp+8]       ; a
;       fadd dword [ebp+12]     ; a + b
;       fdiv dword [val_two]    ; (a+b) / 2
;       pop ebp
;       ret
;
; 答案3：
;   FST存储后保持ST(0)不变（不弹出）
;   FSTP存储后弹出ST(0)（栈顶下移）
;   大多数情况用FSTP，因为计算完后需要清理FPU栈
;   FST用于需要保留中间结果的情况
;
; 答案4：
;   hypotenuse:
;       push ebp
;       mov ebp, esp
;       fld dword [ebp+8]       ; a
;       fmul st0, st0           ; a²
;       fld dword [ebp+12]      ; b
;       fmul st0, st0           ; b²
;       faddp                   ; a² + b²
;       fsqrt                   ; √(a² + b²)
;       pop ebp
;       ret
;
; 答案5：
;   浮点数用二进制表示，有些十进制小数在二进制中是无限循环的。
;   例如 0.1 的二进制是 0.0001100110011...（无限循环）
;   存储时必须截断，导致精度损失。
;   所以 0.1 + 0.2 的结果可能不是精确的 0.3，而是 0.30000000000000004。
;
; ============================================================================
; 教授的话
; ============================================================================
;
; 【核心收获】
;
;   1. FPU有8个80位寄存器，组织成栈结构（ST(0)栈顶）
;   2. FLD加载(压栈)，FST存储(不弹)，FSTP存储并弹栈
;   3. FADD/FSUB/FMUL/FDIV 基本四则运算
;   4. FCOMI 直接设置EFLAGS（比 FCOM+SAHF 更方便）
;   5. FILD/FISTP 整数和浮点互转
;   6. SSE(XMM 128位)/AVX(YMM 256位) 可并行处理多个浮点数
;
; 【常见陷阱】
;
;   1. FPU栈溢出：压入超过8个值会覆盖最底部的数据
;   2. FST 和 FSTP 混用：FSTP会弹栈，栈顶下移
;   3. 整数转浮点用 FILD(从内存加载整数)，不是直接 FLD
;   4. 浮点比较用 FCOMI 而不是 CMP（CMP 只能比较整数）
