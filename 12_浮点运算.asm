; 二、SIMD 简介（SSE/AVX）
; ============================================================================
;
; SIMD（Single Instruction Multiple Data）= 单指令多数据
; 一条指令同时处理多个数据，大大加速并行计算。
;
; SSE寄存器：
;   XMM0 - XMM7，每个128位（16字节）
;   可以同时处理：4个float 或 2个double
;
; AVX寄存器：
;   YMM0 - YMM7，每个256位（32字节）
;   可以同时处理：8个float 或 4个double
;
; 生活类比：
;   标量运算 = 一条流水线一次处理一个产品
;   SIMD运算 = 四条流水线一次处理四个产品
;
; SSE示例（概念）：
;   movaps xmm0, [array1]     ; 加载4个float
;   addps  xmm0, [array2]     ; 4个float同时相加
;   movaps [result], xmm0     ; 存储4个结果
;
; SSE指令需要16字节对齐的内存！
; 用 align 16 来对齐数据

section .data
    align 16
    sse_a   dd 1.0, 2.0, 3.0, 4.0    ; 4个float
    sse_b   dd 5.0, 6.0, 7.0, 8.0    ; 4个float
    sse_res dd 0.0, 0.0, 0.0, 0.0    ; 结果

section .text
    ; SSE加法（需要CPU支持SSE）
    ; movaps  xmm0, [sse_a]   ; 加载4个float到XMM0
    ; addps   xmm0, [sse_b]   ; 4个float同时相加
    ; movaps  [sse_res], xmm0 ; 存储结果
    ; sse_res = 6.0, 8.0, 10.0, 12.0

