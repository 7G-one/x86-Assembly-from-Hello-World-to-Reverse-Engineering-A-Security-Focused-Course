; ============================================================================
; 一、GCC 内联汇编（AT&T语法）
; ============================================================================
;
; GCC使用AT&T语法的内联汇编，格式：
;   asm("汇编指令" : 输出 : 输入 : 破坏列表);
;
; 简单示例（GCC风格）：
;   int result;
;   asm("movl $42, %0" : "=r"(result));
;   // result = 42
;
; 语法说明：
;   %0, %1, %2... = 操作数占位符
;   "r" = 任意寄存器
;   "m" = 内存操作数
;   "i" = 立即数
;   "=" = 只写输出
;   "+" = 读写
;
; 示例：两个数相加
;   int a = 10, b = 20, sum;
;   asm("addl %2, %0" : "=r"(sum) : "0"(a), "r"(b));
;   // sum = a + b

; ============================================================================
; 二、MSVC 内联汇编（Intel语法）
; ============================================================================
;
; Microsoft Visual C++ 使用 __asm 关键字，Intel语法
;
; 示例（MSVC风格，概念展示）：
;
; // 简单加法
; int add_asm(int a, int b) {
;     __asm {
;         mov eax, a
;         add eax, b
;         ; 结果在 eax 中，自动作为返回值
;     }
; }
;
; // 使用变量
; int x = 10;
; __asm {
;     mov eax, x
;     inc eax
;     mov x, eax
; }
; // x = 11

; ============================================================================
; 三、C调用汇编函数
; ============================================================================
;
; C语言可以调用汇编编写的函数，需要遵守调用约定。
;
; 步骤：
;   1. 在汇编中定义函数，使用 global 导出
;   2. 在C中用 extern 声明函数
;   3. 链接时把两者链接在一起
;
; 汇编文件 (math_funcs.asm)：
;   section .text
;   global _add_numbers        ; cdecl约定
;   _add_numbers:
;       mov eax, [esp+4]       ; 参数a
;       add eax, [esp+8]       ; 参数b
;       ret                    ; 返回值在EAX
;
; C文件 (main.c)：
;   extern int add_numbers(int a, int b);
;   int main() {
;       int result = add_numbers(10, 20);
;       printf("Result: %d\n", result);
;       return 0;
; }
;
; 编译：
;   nasm -f win32 math_funcs.asm -o math_funcs.obj
;   cl main.c math_funcs.obj

; ============================================================================
; 四、汇编调用C函数
; ============================================================================
;
; 汇编也可以调用C编写的函数。
;
; C文件 (helper.c)：
;   int factorial(int n) {
;       if (n <= 1) return 1;
;       return n * factorial(n - 1);
;   }
;
; 汇编文件：
;   extern _factorial           ; 声明外部C函数
;
;   section .text
;   global _start
;   _start:
;       push dword 5            ; 参数：5
;       call _factorial          ; 调用C函数
;       add esp, 4              ; 清理栈
;       ; EAX = 120 (5!)

; ============================================================================
; 五、内联汇编的实际应用
; ============================================================================
;
; 1. 性能优化：关键代码用汇编编写
; 2. 特殊指令：访问CPU特殊功能（如CPUID、RDTSC）
; 3. 原子操作：实现无锁数据结构
; 4. 系统编程：访问特殊寄存器、端口IO

section .data
    cpu_vendor  db 13 dup(0)    ; CPU厂商字符串
    timestamp   dq 0

section .text
    extern _ExitProcess@4
    global _start

_start:
    ; ==================================================================
    ; CPUID 指令示例
    ; ==================================================================
    ; CPUID 返回CPU信息
    ; EAX=0：返回厂商字符串

    ; （概念代码，需要在支持的环境中运行）
    ; mov eax, 0
    ; cpuid
    ; ; EBX:EDX:ECX = 厂商字符串
    ; mov [cpu_vendor], ebx
    ; mov [cpu_vendor+4], edx
    ; mov [cpu_vendor+8], ecx

    ; ==================================================================
    ; RDTSC 指令示例
    ; ==================================================================
    ; RDTSC 读取时间戳计数器（用于性能测量）
    ; 返回值在 EDX:EAX

    ; （概念代码）
    ; rdtsc
    ; mov [timestamp], eax
    ; mov [timestamp+4], edx

    ; 退出
    push    0
