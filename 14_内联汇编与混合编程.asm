; 练习1（代码题）：
;   写一个汇编函数 asm_max(int a, int b)，返回两个数中的最大值。
;   在C中调用它。
;
; 练习2（概念题）：
;   GCC内联汇编和MSVC内联汇编有什么区别？
;
; 练习3（代码题）：
;   写一个汇编函数 asm_memcpy(void *dst, void *src, int size)，
;   使用字符串指令实现内存复制。
;
; 练习4（概念题）：
;   为什么内联汇编需要"破坏列表"（clobber list）？
;
; 练习5（思考题）：
;   什么情况下应该使用内联汇编？什么情况下应该使用独立的汇编文件？
;
; ============================================================================
; 参考答案
; ============================================================================
;
; 答案1：
;   ; 汇编文件
;   global _asm_max
;   _asm_max:
;       mov eax, [esp+4]
;       mov ecx, [esp+8]
;       cmp eax, ecx
;       jge .done
;       mov eax, ecx
;   .done:
;       ret
;
;   // C文件
;   extern int asm_max(int a, int b);
;   int main() {
;       printf("Max: %d\n", asm_max(10, 20));
;       return 0;
;   }
;
; 答案2：
;   GCC使用AT&T语法（操作数顺序：源, 目标），MSVC使用Intel语法（目标, 源）
;   GCC用 %0, %1 占位符，MSVC直接使用变量名
;   GCC需要指定输入/输出/破坏列表，MSVC自动处理
;   GCC更灵活但更复杂，MSVC更简单但功能较少
;
; 答案3：
;   global _asm_memcpy
;   _asm_memcpy:
;       push ebp
;       mov ebp, esp
;       push esi
;       push edi
;       push ecx
;       mov edi, [ebp+8]
;       mov esi, [ebp+12]
;       mov ecx, [ebp+16]
;       cld
;       rep movsb
;       pop ecx
;       pop edi
;       pop esi
;       pop ebp
;       ret
;
; 答案4：
;   告诉编译器哪些寄存器被汇编代码修改了。
;   如果不声明，编译器可能认为某些寄存器的值没变，导致错误。
;   例如：汇编修改了EAX，但编译器认为EAX还是原来的值。
;
; 答案5：
;   内联汇编适合：少量关键指令、需要访问C变量、简单优化
;   独立文件适合：大量汇编代码、需要独立编译、性能关键模块
;   通常建议尽量少用汇编，只在必要时使用。
;
; ============================================================================
; 教授的话
; ============================================================================
;
; 【核心收获】
;
;   1. GCC用AT&T语法(源,目标)，MSVC用Intel语法(目标,源)
;   2. C调用汇编：汇编用 global 导出，C用 extern 声明
;   3. 汇编调用C：汇编用 extern 声明，按调用约定传参
;   4. 内联汇编适合少量关键指令（如CPUID、RDTSC）
;   5. 调用约定必须匹配（cdecl/stdcall），否则栈不平衡
;
; 【常见陷阱】
;
;   1. AT&T和Intel的操作数顺序相反，混用会写反
;   2. 函数名修饰：cdecl加_前缀，stdcall加@N后缀
;   3. GCC内联汇编必须写破坏列表(clobber list)，否则编译器优化出错
;   4. 混合编程时注意32位/64位一致性
