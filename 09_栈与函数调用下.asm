; ============================================================================
; 一、栈帧（Stack Frame）
; ============================================================================
;
; 每次函数调用都会在栈上创建一个"栈帧"，包含：
;   - 函数的参数
;   - 返回地址
;   - 保存的旧EBP（帧指针）
;   - 局部变量
;   - 保存的寄存器
;
; 生活类比：
;   栈帧就像一张"工作台"：
;   每次你开始一项新工作（函数调用），你就在桌上铺一张新的桌布（新栈帧）。
;   桌布上放着你需要的材料（参数）、说明书（返回地址）、和工具（局部变量）。
;   工作结束后，你把桌布收起来（清理栈帧），回到上一项工作。
;
; 栈帧结构（从高地址到低地址）：
;
;   高地址
;   ┌──────────────────────┐
;   │  参数 n              │ [EBP + 8 + 4*(n-1)]
;   ├──────────────────────┤
;   │  ...                 │
;   ├──────────────────────┤
;   │  参数 2              │ [EBP + 12]
;   ├──────────────────────┤
;   │  参数 1              │ [EBP + 8]
;   ├──────────────────────┤
;   │  返回地址            │ [EBP + 4]
;   ├──────────────────────┤
;   │  保存的旧 EBP        │ [EBP]  <- EBP指向这里
;   ├──────────────────────┤
;   │  局部变量 1          │ [EBP - 4]
;   ├──────────────────────┤
;   │  局部变量 2          │ [EBP - 8]
;   ├──────────────────────┤
;   │  保存的寄存器        │
;   ├──────────────────────┤
;   │  ...                 │  <- ESP指向栈顶
;   └──────────────────────┘
;   低地址
;
; EBP 和 ESP 的区别：
;   ESP = 栈顶指针，随时变化（push/pop会改变它）
;   EBP = 帧指针，在函数执行期间保持不变（方便访问参数和局部变量）

section .data
    fmt     db 'Result: %d', 0x0A, 0

section .text
    extern _ExitProcess@4
    global _start

_start:
    ; ==================================================================
    ; 标准函数序言（Prologue）和尾声（Epilogue）
    ; ==================================================================

    ; 调用一个完整的函数
    push    20                  ; 参数 b
    push    10                  ; 参数 a
    call    add_with_locals
    add     esp, 8              ; 清理参数（cdecl）

    ; EAX = 10 + 20 + 100 = 130

    ; 退出程序
    push    0
    call    _ExitProcess@4

; ============================================================================
; 完整的函数示例：带局部变量
; ============================================================================
; int add_with_locals(int a, int b) {
;     int temp = 100;      // 局部变量
;     int result = a + b + temp;
;     return result;
; }

add_with_locals:
    ; ---- 函数序言（Prologue）----
    push    ebp                 ; 保存调用者的EBP
    mov     ebp, esp            ; 建立新的栈帧（EBP = ESP）
    sub     esp, 8              ; 分配2个局部变量的空间（2 * 4 = 8字节）
    ; 现在栈的状态：
    ; [EBP+8]  = 参数 a
    ; [EBP+12] = 参数 b
    ; [EBP+4]  = 返回地址
    ; [EBP]    = 保存的旧EBP
    ; [EBP-4]  = 局部变量 temp
    ; [EBP-8]  = 局部变量 result
    ; ESP      = EBP - 8

    ; ---- 函数体 ----
    mov     dword [ebp - 4], 100        ; temp = 100

    mov     eax, [ebp + 8]              ; eax = a
    add     eax, [ebp + 12]             ; eax += b
    add     eax, [ebp - 4]              ; eax += temp
    mov     [ebp - 8], eax              ; result = a + b + temp

    ; 返回值放入EAX
    mov     eax, [ebp - 8]

    ; ---- 函数尾声（Epilogue）----
    mov     esp, ebp            ; 释放局部变量（ESP = EBP）
    pop     ebp                 ; 恢复调用者的EBP
    ret                         ; 返回

; ============================================================================
; 二、详细解析栈帧操作
; ============================================================================
;
; 让我们一步步跟踪栈的变化：
;
; 调用 add_with_locals(10, 20) 时：
;
; 步骤1: push 20（参数b）
;   栈: [20]
;
; 步骤2: push 10（参数a）
;   栈: [10][20]
;
; 步骤3: call add_with_locals
;   - 自动push返回地址
;   栈: [返回地址][10][20]
;
; 步骤4: push ebp（保存旧EBP）
;   栈: [旧EBP][返回地址][10][20]
;
; 步骤5: mov ebp, esp（建立栈帧）
;   EBP = ESP（指向保存的旧EBP）
;
; 步骤6: sub esp, 8（分配局部变量空间）
;   栈: [局部变量2][局部变量1][旧EBP][返回地址][10][20]
;
; 步骤7: 函数执行...
;
; 步骤8: mov esp, ebp（释放局部变量）
;   ESP回到EBP位置
;
; 步骤9: pop ebp（恢复旧EBP）
;   栈: [返回地址][10][20]
;
; 步骤10: ret（返回）
;   - 自动pop返回地址
;   栈: [10][20]
;
; 步骤11: add esp, 8（调用者清理参数）
;   栈: []（平衡了！）

; ============================================================================
; 三、LEA 在栈帧中的应用
; ============================================================================
; LEA 常用于计算局部变量或参数的地址

lea_example:
    push    ebp
    mov     ebp, esp
    sub     esp, 16             ; 分配4个局部变量

    ; 计算局部变量的地址
    lea     eax, [ebp - 4]      ; EAX = &local_var1
    lea     ebx, [ebp - 8]      ; EBX = &local_var2

    ; 计算参数的地址
    lea     ecx, [ebp + 8]      ; ECX = &param1

    mov     esp, ebp
    pop     ebp
    ret

; ============================================================================
; 四、保存和恢复寄存器
; ============================================================================
; 被调用者保存的寄存器：EBX, ESI, EDI, EBP
; 如果函数要使用这些寄存器，必须先保存，返回前恢复

safe_function:
    push    ebp
    mov     ebp, esp
    sub     esp, 4              ; 局部变量空间

    ; 保存需要使用的 callee-saved 寄存器
    push    ebx
    push    esi
    push    edi

    ; 现在可以安全使用 EBX, ESI, EDI
    mov     ebx, 100
    mov     esi, 200
    mov     edi, 300
    ; ... 做一些计算 ...

    ; 恢复寄存器（注意顺序相反！）
    pop     edi
    pop     esi
    pop     ebx

    mov     esp, ebp
    pop     ebp
    ret

; ============================================================================
; 五、递归函数
; ============================================================================
; 递归就是函数调用自身。
; 每次递归调用都会创建一个新的栈帧。

; 阶乘函数：int factorial(int n) {
;     if (n <= 1) return 1;
;     return n * factorial(n-1);
; }

factorial:
    push    ebp
    mov     ebp, esp

    mov     eax, [ebp + 8]      ; eax = n
    cmp     eax, 1              ; n <= 1?
    jle     .base_case          ; 基本情况

    ; 递归情况：n * factorial(n-1)
    dec     eax                 ; n - 1
    push    eax                 ; 保存参数（临时）
    call    factorial            ; factorial(n-1)
    add     esp, 4              ; 清理参数

    ; EAX = factorial(n-1)
    imul    eax, [ebp + 8]      ; EAX = n * factorial(n-1)
    jmp     .factorial_done

.base_case:
    mov     eax, 1              ; 返回1

.factorial_done:
    pop     ebp
    ret

; 调用 factorial(5)
_call_factorial:
    push    5
    call    factorial
    add     esp, 4
    ; EAX = 120 (5! = 5*4*3*2*1)

; ============================================================================
; 六、栈平衡的重要性
; ============================================================================
;
; 栈平衡 = 函数返回时ESP必须等于调用前的值
;
; 如果栈不平衡：
;   1. RET会跳转到错误的地址（因为栈顶不是返回地址）
;   2. 后续的POP会弹出错误的值
;   3. 程序崩溃或产生不可预测的行为
;
; 常见的栈不平衡原因：
;   - PUSH后忘记POP
;   - CALL后忘记清理参数（cdecl约定）
;   - 函数中分支路径不一致（一个分支push了，另一个没有）

; 错误示例（不要这样写！）
bad_function:
    push    ebx                 ; 保存EBX
    test    eax, eax
    jz      .error_path
    ; 正常路径
    mov     eax, 1
    pop     ebx                 ; 恢复EBX
    ret
.error_path:
    mov     eax, -1
    ; 忘记 pop ebx！栈不平衡！
    ret                         ; 这里会出问题！

; 正确示例
good_function:
    push    ebx
    test    eax, eax
    jz      .error_path_ok
    mov     eax, 1
    jmp     .cleanup            ; 跳到统一的清理代码
.error_path_ok:
    mov     eax, -1
.cleanup:
    pop     ebx                 ; 两条路径都经过这里
    ret

; ============================================================================
; 七、ENTER 和 LEAVE 指令
; ============================================================================
; ENTER 和 LEAVE 是栈帧操作的快捷指令

; ENTER size, 0 等价于：
;   push ebp
;   mov ebp, esp
;   sub esp, size

; LEAVE 等价于：
;   mov esp, ebp
;   pop ebp

modern_function:
    enter   16, 0               ; 建立栈帧，分配16字节局部变量
    ; ... 函数体 ...
    leave                       ; 销毁栈帧
    ret

; 现代编译器通常不用ENTER/LEAVE，因为分开写更灵活

