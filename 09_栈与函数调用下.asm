; 练习题
; ============================================================================
;
; 练习1（代码题）：
;   写一个函数 swap(int *a, int *b)，交换两个整数的值。
;   提示：参数是指针（地址）。
;
; 练习2（代码题）：
;   写一个递归函数 fibonacci(n)，返回第n个斐波那契数。
;   F(0)=0, F(1)=1, F(n)=F(n-1)+F(n-2)
;
; 练习3（概念题）：
;   画出调用 func(1, 2, 3) 时栈帧的详细布局。
;   假设 func 有2个局部变量。
;
; 练习4（代码题）：
;   写一个函数 array_sum(int arr[], int n)，返回数组前n个元素的和。
;
; 练习5（思考题）：
;   为什么递归太深会导致栈溢出？栈溢出后会发生什么？
;
; ============================================================================
; 参考答案
; ============================================================================
;
; 答案1：
;   swap:
;       push ebp
;       mov ebp, esp
;       mov eax, [ebp+8]      ; a的地址
;       mov ebx, [ebp+12]     ; b的地址
;       mov ecx, [eax]        ; *a
;       mov edx, [ebx]        ; *b
;       mov [eax], edx        ; *a = *b
;       mov [ebx], ecx        ; *b = *a
;       pop ebp
;       ret
;
; 答案2：
;   fibonacci:
;       push ebp
;       mov ebp, esp
;       mov eax, [ebp+8]      ; n
;       cmp eax, 1
;       jle .base
;       dec eax
;       push eax
;       call fibonacci
;       add esp, 4
;       push eax              ; 保存 fib(n-1)
;       mov eax, [ebp+8]
;       sub eax, 2
;       push eax
;       call fibonacci
;       add esp, 4
;       pop ebx               ; 恢复 fib(n-1)
;       add eax, ebx          ; fib(n-1) + fib(n-2)
;       jmp .done
;   .base:
;       ; eax已经是n（0或1）
;   .done:
;       pop ebp
;       ret
;
; 答案3：
;   高地址
;   [参数3: 3]     [EBP+16]
;   [参数2: 2]     [EBP+12]
;   [参数1: 1]     [EBP+8]
;   [返回地址]      [EBP+4]
;   [旧EBP]         [EBP] <- EBP
;   [局部变量1]     [EBP-4]
;   [局部变量2]     [EBP-8] <- ESP
;   低地址
;
; 答案4：
;   array_sum:
;       push ebp
;       mov ebp, esp
;       push ebx
;       mov esi, [ebp+8]      ; arr指针
;       mov ecx, [ebp+12]     ; n
;       xor eax, eax          ; sum = 0
;       xor edx, edx          ; i = 0
;   .loop:
;       cmp edx, ecx
;       jge .done
;       add eax, [esi + edx*4]
;       inc edx
;       jmp .loop
;   .done:
;       pop ebx
;       pop ebp
;       ret
;
; 答案5：
;   每次递归调用都会创建新的栈帧（保存EBP、返回地址、局部变量等）。
;   栈空间是有限的（通常1-8MB），递归太深会用完栈空间。
;   栈溢出后，程序会访问非法内存，触发段错误(Segmentation Fault)。
;   在Windows上是"栈溢出"异常，在Linux上是SIGSEGV信号。
;
; ============================================================================
; 教授的话
; ============================================================================
;
; 【核心收获】
;
;   1. 栈帧 = 函数在栈上的"工作区"，每次调用创建一个
;   2. EBP 帧指针不变（方便访问参数和局部变量），ESP 栈顶指针随push/pop变化
;   3. 函数序言：push ebp; mov ebp, esp; sub esp, N
;   4. 函数尾声：mov esp, ebp; pop ebp; ret
;   5. 参数在 [EBP+8] 以上，局部变量在 [EBP-4] 以下
;   6. 递归 = 函数调用自身，每次创建新栈帧，太深会栈溢出
;
; 【常见陷阱】
;
;   1. 忘记函数序言/尾声，EBP 被破坏导致参数访问错误
;   2. 栈不平衡：某个分支 push 了但没 pop，ret 跳到错误地址
;   3. 局部变量空间 sub esp, N 中 N 必须是4的倍数（对齐）
;   4. 递归没有终止条件，或者终止条件写错，导致无限递归
;
