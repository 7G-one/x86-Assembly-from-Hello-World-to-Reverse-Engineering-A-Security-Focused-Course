; 一、什么是中断？
; ============================================================================
;
; 中断(Interrupt)是CPU暂停当前工作，去处理紧急事件的机制。
;
; 生活类比：
;   你正在看书（执行程序），突然：
;   - 电话响了（硬件中断）-> 你接电话 -> 回来继续看书
;   - 你想上厕所（软件中断/系统调用）-> 你主动暂停 -> 回来继续看书
;   - 闹钟响了（定时器中断）-> 你看时间 -> 回来继续看书
;
; 中断类型：
;   1. 硬件中断（外部中断）：由外部设备触发（键盘、鼠标、网卡等）
;   2. 软件中断：由程序主动触发（INT指令）
;   3. 异常：CPU内部事件（除零、页错误等）
;
; 中断处理流程：
;   1. CPU收到中断信号
;   2. 保存当前状态（EIP, EFLAGS, CS等压栈）
;   3. 根据中断号查找中断向量表（IDT）
;   4. 跳转到对应的中断处理程序（ISR）
;   5. 执行中断处理程序
;   6. 执行 IRET 返回，恢复之前的状态
;
; 生活类比：
;   就像你在做饭（执行程序），突然有人敲门（中断）：
;   1. 你记住做到哪一步了（保存状态）
;   2. 去开门（执行中断处理）
;   3. 处理完后回到厨房，从刚才的步骤继续（IRET恢复）

section .data
    msg     db 'Hello from syscall!', 0x0A, 0
    msg_len equ $ - msg

section .text
    ; ==================================================================
    ; INT 指令 - 软件中断
    ; ==================================================================
    ; INT n 触发n号中断
    ;
    ; 在Linux中，INT 0x80 是系统调用入口
    ; 在Windows中，通常使用API调用而不是INT指令
    ;
    ; Linux系统调用示例（32位）：
    ;   EAX = 系统调用号
    ;   EBX = 第一个参数
    ;   ECX = 第二个参数
    ;   EDX = 第三个参数
    ;   INT 0x80

    ; Linux write(stdout, msg, len) 示例
    ; mov eax, 4              ; sys_write 的调用号
    ; mov ebx, 1              ; stdout 文件描述符
    ; mov ecx, msg            ; 字符串地址
    ; mov edx, msg_len        ; 字符串长度
    ; int 0x80                ; 触发系统调用

    ; Linux exit(0) 示例
    ; mov eax, 1              ; sys_exit 的调用号
    ; xor ebx, ebx            ; 退出码 0
    ; int 0x80

    ; ==================================================================
    ; Windows API 调用
    ; ==================================================================
    ; Windows不直接使用INT指令，而是通过DLL导出的函数
    ; 这些函数最终会调用 ntoskrnl.exe 中的系统服务
    ;
    ; 调用链：
    ;   用户程序 -> kernel32.dll -> ntdll.dll -> 内核
    ;
    ; ntdll.dll 中有一个关键函数 NtDll!KiFastSystemCall：
    ;   mov eax, 系统调用号
    ;   mov edx, 7FFE0300h     ; SharedUserData->SystemCall
    ;   call [edx]              ; 或 sysenter/sysexit
    ;
    ; sysenter/sysexit 是比INT更快的系统调用方式

    ; Windows API调用示例（通过kernel32.dll）
    extern _GetStdHandle@4
    extern _WriteConsoleA@20
    extern _ExitProcess@4

    global _start

_start:
    ; 获取标准输出句柄
    push    -11                 ; STD_OUTPUT_HANDLE
    call    _GetStdHandle@4

    ; 输出字符串
    push    0
    push    0
    push    msg_len
    push    msg
    push    eax
    call    _WriteConsoleA@20

    ; 退出
    push    0
    call    _ExitProcess@4

; ============================================================================
; 二、中断向量表（IDT）
; ============================================================================
;
; IDT（Interrupt Descriptor Table）是中断号和处理函数的映射表。
; 有256个条目（0-255），每个条目指向一个中断处理函数。
;
; 常见中断号：
;   0x00 - 除法错误（Divide Error）
;   0x01 - 单步调试（Debug）
;   0x03 - 断点（Breakpoint）-> int 3
;   0x06 - 非法指令
;   0x0D - 一般保护错误
;   0x0E - 页错误
;   0x80 - Linux系统调用
;   0x2E - Windows系统调用（旧版）
;
; 生活类比：
;   IDT就像一个电话簿：
;   中断号 = 电话号码
;   处理函数 = 对应的人
;   有紧急情况时，根据号码找到对应的人来处理

; ============================================================================
; 三、系统调用详解
; ============================================================================
;
; 系统调用 = 用户程序请求操作系统服务
;
; 可以请求的服务：
;   - 文件操作（打开、读、写、关闭）
;   - 进程管理（创建、终止、等待）
;   - 内存管理（分配、释放）
;   - 网络通信
;   - 设备访问
;
; Windows系统调用号（部分，可能会因版本变化）：
;   0x0000 - NtAcceptConnectPort
;   0x0001 - NtAccessCheck
;   ...
;   0x0100 - NtCreateFile
;   0x010E - NtCreateProcess
;   ...
;
; 注意：直接使用系统调用号不稳定，因为Windows不同版本号不同。
; 推荐使用API函数（kernel32.dll等），它们会处理版本差异。

; ============================================================================
; 四、SEH 结构化异常处理
; ============================================================================
;
; Windows使用SEH（Structured Exception Handling）处理异常。
; FS:[0] 指向SEH链的头节点。
;
; SEH链结构：
;   [FS:0] -> [下一个SEH节点] -> [下一个] -> ... -> -1
;
; 每个SEH节点：
;   [0] - 指向下一个节点的指针
;   [4] - 异常处理函数的地址
;
; 当异常发生时，系统遍历SEH链，找到能处理该异常的函数。

section .data
    seh_msg     db 'Exception caught!', 0x0A, 0
    seh_msg_len equ $ - seh_msg

section .text
    ; SEH安装示例（概念）
    ; push    handler         ; 异常处理函数地址
    ; push    dword [fs:0]    ; 保存旧的SEH链头
    ; mov     [fs:0], esp     ; 安装新的SEH节点

    ; 异常处理函数（概念）
    ; handler:
    ;     ; 参数在栈上
    ;     ; [esp+4] = EXCEPTION_RECORD*
    ;     ; [esp+8] = 建立帧的指针
    ;     ; [esp+12] = CONTEXT*
    ;     ; [esp+16] = 保留
    ;
    ;     ; 处理异常...
    ;
    ;     ; 恢复旧的SEH链
    ;     mov     esp, [ebp]
    ;     pop     dword [fs:0]
    ;
    ;     mov     eax, 1      ; ExceptionContinueExecution
    ;     ret

; ============================================================================
; 五、调试中断
; ============================================================================
;
; INT 3（0xCC）是调试器使用的断点指令。
;
; 当调试器在某行设置断点时，它会：
;   1. 保存该地址的原始字节
;   2. 把该字节替换为 0xCC（INT 3）
;   3. 当程序执行到这里时，触发中断
;   4. 调试器获得控制权
;   5. 恢复原始字节，让你检查程序状态
;
; 这就是为什么在调试器中按F9设置断点后，程序会暂停。

section .text
; 注意：以下代码为独立参考段，不参与主程序执行流程
_start_debug:
    ; 手动触发断点
    int3                        ; 触发3号中断（调试器会暂停）
    ; 如果没有调试器，程序可能会崩溃或被SEH捕获

    ; INT 3 的机器码是 0xCC（1字节）
    ; 可以用 db 0xCC 手动插入断点

; ============================================================================
; 六、实模式 vs 保护模式
; ============================================================================
;
; x86 CPU有两种主要运行模式：
;
; 实模式（Real Mode）：
;   - 启动时的默认模式
;   - 16位，只能访问1MB内存
;   - 没有内存保护
;   - DOS运行在这种模式
;
; 保护模式（Protected Mode）：
;   - 现代操作系统的模式
;   - 32位，可以访问4GB内存
;   - 有内存保护、权限级别
;   - Windows/Linux运行在这种模式
;
; 从实模式切换到保护模式的步骤：
;   1. 禁用中断
;   2. 设置GDT（全局描述符表）
;   3. 设置CR0寄存器的PE位
;   4. 远跳转刷新流水线
;   5. 设置段寄存器
;   6. 设置IDT
;   7. 启用中断
;
; 这就是操作系统启动时做的事情！

; ============================================================================
; 练习题
; ============================================================================
;
; 练习1（概念题）：
;   解释中断处理的完整流程，从CPU收到中断到返回。
;
; 练习2（概念题）：
;   硬件中断、软件中断和异常有什么区别？各举一个例子。
;
; 练习3（代码题）：
;   写一段Linux 32位汇编代码，使用INT 0x80实现：
;   a) 输出"Hello"到标准输出
;   b) 退出程序，返回码为42
;
; 练习4（概念题）：
;   为什么Windows不推荐直接使用系统调用号，而是使用API函数？
;
; 练习5（思考题）：
;   调试器是如何利用INT 3实现断点功能的？
;
; ============================================================================
; 参考答案
; ============================================================================
;
; 答案1：
;   1. CPU收到中断信号
;   2. CPU完成当前指令
;   3. CPU保存状态到栈：EFLAGS, CS, EIP（有时还有错误码）
;   4. CPU根据中断号在IDT中查找处理函数地址
;   5. CPU跳转到中断处理函数
;   6. 处理函数执行（可能保存更多寄存器）
;   7. 处理函数执行IRET
;   8. CPU恢复EIP, CS, EFLAGS
;   9. 继续执行被中断的程序
;
; 答案2：
;   硬件中断：由外部设备触发，异步，不可预测
;     例子：键盘按键、网卡收到数据
;   软件中断：由程序主动触发，同步
;     例子：INT 0x80（Linux系统调用）
;   异常：由CPU内部事件触发，同步
;     例子：除零错误、页错误
;
; 答案3：
;   section .data
;   msg db 'Hello', 0x0A
;   section .text
;   global _start
;   _start:
;       mov eax, 4          ; sys_write
;       mov ebx, 1          ; stdout
;       mov ecx, msg
;       mov edx, 6          ; 长度
;       int 0x80
;       mov eax, 1          ; sys_exit
;       mov ebx, 42         ; 返回码
;       int 0x80
;
; 答案4：
;   系统调用号在不同Windows版本中可能变化。
;   API函数（如kernel32.dll的导出函数）提供了稳定的接口。
;   API函数内部会处理版本差异，确保程序在不同Windows版本上都能运行。
;
; 答案5：
;   调试器在断点地址放置0xCC（INT 3指令）。
;   当CPU执行到这里时，触发3号中断。
;   操作系统通知调试器，调试器获得控制权。
;   调试器恢复原始指令，显示程序状态，等待用户操作。
;
; ============================================================================
; 教授的话
; ============================================================================
;
; 【核心收获】
;
;   1. 中断 = CPU暂停当前工作去处理紧急事件（硬件/软件/异常）
;   2. IDT(中断描述符表) = 256个中断号到处理函数的映射
;   3. Linux用 INT 0x80 系统调用，Windows用 API 函数（kernel32.dll）
;   4. Windows调用链：用户程序 -> kernel32 -> ntdll -> 内核
;   5. SEH = Windows结构化异常处理，FS:[0] 指向SEH链
;   6. INT 3 (0xCC) = 调试器断点原理（替换指令为0xCC）
;
; 【常见陷阱】
;
;   1. 不要直接用系统调用号（Windows不同版本号不同），用API函数
;   2. INT 0x80 是Linux专用，Windows下会崩溃
;   3. 中断处理函数要用 IRET 返回（不是 RET），否则栈不平衡
;   4. SEH链断开会导致异常无法捕获，程序直接崩溃
