; ============================================================================
; 参考答案
; ============================================================================
;
; 答案1：
;   溢出前：                  溢出后：
;   [参数]                    [参数]
;   [返回地址]                [被覆盖的返回地址] <- 攻击者控制
;   [保存的EBP]               [被覆盖的EBP]
;   [buffer]                  [AAAA...AAAA]
;
; 答案2：
;   void vuln() {
;       char buf[32];
;       gets(buf);
;   }
;   偏移 = 32(buf) + 4(EBP) = 36字节
;   payload = 'A'*36 + 新返回地址
;
; 答案3：
;   NX：标记栈/堆为不可执行，注入的shellcode无法运行
;   ASLR：每次运行随机化内存地址，无法预测shellcode地址
;   Stack Canary：在返回地址前放随机值，溢出时会被覆盖，检测到攻击
;
; 答案4：
;   1. 先用格式化字符串漏洞泄露某个libc函数的地址
;   2. 根据泄露的地址计算libc基址
;   3. 计算system和"/bin/sh"的地址
;   4. 构造payload：填充 + system地址 + 假返回地址 + "/bin/sh"地址
;
; 答案5：
;   1. 找到pop rdi; ret gadget（设置第一个参数）
;   2. 找到"/bin/sh"字符串的地址（或在栈上构造）
;   3. 找到system函数的地址
;   4. ROP链：[pop rdi; ret] ["/bin/sh"地址] [system地址]
;   执行流程：pop rdi <- "/bin/sh"地址, 然后跳到system
;
; ============================================================================
; 教授的话
; ============================================================================
;
; 【核心收获】
;
;   1. 栈溢出 = 写入超过缓冲区大小，覆盖返回地址控制程序流程
;   2. payload = 填充数据(覆盖buffer+EBP) + 新返回地址
;   3. 偏移计算：buffer大小 + 4(EBP) = 填充长度
;   4. 防护：Stack Canary(随机值检测)、NX(栈不可执行)、ASLR(地址随机化)
;   5. 绕过：ret2libc(跳到已有库函数)、ROP(串联代码片段)
;
; 【常见陷阱】
;
;   1. 偏移算错会导致覆盖不完整或覆盖过多，程序行为异常
;   2. ASLR开启后每次运行地址不同，需要先泄露地址
;   3. Canary被覆盖会触发 __stack_chk_fail，程序直接终止
;   4. NX保护下不能直接执行栈上的shellcode，需要ROP或ret2libc
