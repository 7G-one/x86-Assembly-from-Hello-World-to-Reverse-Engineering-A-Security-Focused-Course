; 七、栈溢出检测函数
; ============================================================================
;
; 以下函数演示如何检测栈溢出：

check_canary:
    push    ebp
    mov     ebp, esp
    sub     esp, 64             ; 缓冲区

    ; 在EBP前放置一个canary值
    mov     dword [ebp - 4], 0xDEADBEEF  ; canary

    ; ... 正常函数体 ...

    ; 返回前检查canary
    cmp     dword [ebp - 4], 0xDEADBEEF
    jne     .canary_triggered   ; canary被修改了！
    ; 正常返回
    mov     esp, ebp
    pop     ebp
    ret

.canary_triggered:
    ; 栈溢出检测到！
    ; 在实际系统中会调用 __stack_chk_fail
    push    0
    call    _ExitProcess@4

