; 一、什么是栈溢出？
; ============================================================================
;
; 栈溢出(Buffer Overflow)是指向缓冲区写入的数据超过了它的容量，
; 多出的数据会覆盖栈上的其他内容（如返回地址）。
;
; 生活类比：
;   想象你有一个杯子（缓冲区），只能装200ml水。
;   如果有人往里面倒了500ml水（输入过多），
;   多出的300ml水会溢出来，流到桌子上（覆盖其他数据）。
;   如果桌上放着重要的文件（返回地址），就会被水弄湿（被覆盖）。
;
; 栈布局（函数调用时）：
;
;   高地址
;   ┌──────────────────────┐
;   │  参数                │
;   ├──────────────────────┤
;   │  返回地址            │ <- 攻击目标！
;   ├──────────────────────┤
;   │  保存的EBP           │
;   ├──────────────────────┤
;   │  缓冲区[0..n]        │ <- 输入数据
;   ├──────────────────────┤
;   │  其他局部变量        │
;   └──────────────────────┘
;   低地址
;
; 如果输入超过缓冲区大小，会依次覆盖：
;   其他局部变量 -> 保存的EBP -> 返回地址 -> 参数

; ============================================================================
; 二、一个有漏洞的程序
; ============================================================================
;
; 以下是一个典型的有缓冲区溢出漏洞的程序：
;
; C代码：
;   #include <stdio.h>
;   void vulnerable_function() {
;       char buffer[64];      // 64字节的缓冲区
;       gets(buffer);         // 危险！不检查输入长度
;   }
;   int main() {
;       vulnerable_function();
;       return 0;
;   }
;
; 漏洞分析：
;   gets() 不检查输入长度，可以输入任意多的数据。
;   输入超过64字节就会溢出buffer，覆盖返回地址。

section .data
    ; 漏洞程序模拟
    prompt      db 'Enter input: ', 0
    overflow_msg db 'Buffer overflow!', 0x0A, 0
    normal_msg  db 'Normal return', 0x0A, 0

section .bss
    buffer      resb 64         ; 64字节缓冲区

section .text
    extern _ExitProcess@4
    global _start

_start:
    ; ==================================================================
    ; 模拟有漏洞的函数
    ; ==================================================================
    ; 我们手动模拟栈溢出的过程

    call    vulnerable_func
    ; 正常情况下，程序会回到这里

    ; 退出
    push    0
    call    _ExitProcess@4

; 有漏洞的函数
vulnerable_func:
    push    ebp
    mov     ebp, esp
    sub     esp, 64             ; 分配64字节缓冲区

    ; 模拟正常输入（不溢出）
    lea     edi, [ebp - 64]     ; buffer地址
    mov     byte [edi], 'A'     ; 写入1字节，安全

    ; 如果输入超过64字节，会覆盖：
    ; [ebp - 64] 到 [ebp - 1]  : buffer（64字节）
    ; [ebp]                     : 保存的EBP
    ; [ebp + 4]                 : 返回地址 <- 覆盖这里就能控制程序流程！

    ; 模拟溢出
    lea     edi, [ebp - 64]
    mov     ecx, 68             ; 写入68字节（超过4字节）
    mov     al, 'A'
.fill_loop:
    stosb                       ; [EDI++] = 'A'
    dec     ecx
    jnz     .fill_loop
    ; 现在返回地址被覆盖为 0x41414141 ('AAAA')

    ; 正常返回（但如果返回地址被覆盖，会跳到错误的地方）
    mov     esp, ebp
    pop     ebp
    ret                         ; 这里会跳转到被覆盖的地址！

; ============================================================================
; 三、攻击原理
; ============================================================================
;
; 攻击步骤：
;   1. 确定缓冲区大小和返回地址的偏移
;   2. 构造payload：
;      [填充数据（覆盖缓冲区和EBP）][新返回地址]
;   3. 新返回地址指向攻击者控制的代码（如shellcode）
;
; 偏移计算：
;   buffer大小 + EBP大小 = 填充长度
;   例如：64 + 4 = 68字节填充 + 4字节新返回地址
;
; Payload结构：
;   'A' * 68 + 新返回地址(4字节)
;   或
;   'A' * 68 + shellcode地址 + shellcode

; ============================================================================
; 四、防护机制
; ============================================================================
;
; 现代操作系统有多种防护机制：
;
; 1. 栈保护（Stack Canary / GS）
;    - 在EBP和返回地址之间放一个随机值（canary）
;    - 函数返回前检查canary是否被修改
;    - 如果被修改，说明发生了溢出，程序终止
;    - 就像在杯子旁边放一个探测器，水溢出来就报警
;
; 2. NX/DEP（不可执行栈）
;    - 标记栈/堆内存为不可执行
;    - 即使注入shellcode也无法执行
;    - 就像给仓库装了防火门，着火了也烧不到外面
;
; 3. ASLR（地址空间布局随机化）
;    - 每次运行程序，内存地址都不同
;    - 攻击者无法预测shellcode的地址
;    - 就像每次搬家，地址都变了
;
; 4. PIE（位置无关可执行文件）
;    - 程序本身也是随机地址加载
;    - 配合ASLR效果更好
;
; 5. RELRO（重定位只读）
;    - 保护GOT表不被修改
;    - 防止GOT覆写攻击

; ============================================================================
; 五、绕过技术简介
; ============================================================================
;
; 1. Return-to-libc / ret2libc
;    - 不执行shellcode，而是跳转到已有的库函数
;    - 例如跳转到 system("/bin/sh")
;    - 绕过NX保护
;
; 2. ROP（Return-Oriented Programming）
;    - 在已有代码中找到有用的"片段"(gadget)
;    - 每个gadget以ret结尾
;    - 通过控制栈上的返回地址，串联多个gadget
;    - 可以绕过NX
;
; 3. 泄露canary
;    - 通过格式化字符串漏洞等泄露canary值
;    - 然后在溢出时填入正确的canary
;
; 4. 泄露地址
;    - 先泄露某个已知地址（如puts的GOT地址）
;    - 计算出libc基址
;    - 然后调用system

