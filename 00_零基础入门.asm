; 一、什么是汇编语言？
; ============================================================================
;
; 想象一下你和一个外国人交流：
;   - 高级语言（如C、Python）= 你用中文写信，翻译帮你翻成英文
;   - 汇编语言 = 你直接用英文（虽然语法简单，但每个词都要自己写）
;   - 机器语言 = 01010101...（纯数字，人脑几乎无法阅读）
;
; 汇编语言是机器语言的"人类可读版"：
;   - 机器语言：10111000 00000001 00000000 00000000  （CPU直接执行的二进制）
;   - 汇编语言：mov eax, 1                           （人类能看懂的助记符）
;
; 它们之间是一一对应的！每一个汇编指令都对应一条机器指令。
;
; ============================================================================
; 二、为什么要学汇编？
; ============================================================================
;
; 1. 理解计算机底层原理 - 知道程序到底是怎么跑的
; 2. 性能优化 - 关键代码用汇编可以做到极致优化
; 3. 逆向工程/安全研究 - CTF、漏洞挖掘、恶意软件分析都离不开汇编
; 4. 嵌入式开发 - 单片机、驱动程序开发
; 5. 调试能力 - 看懂反汇编才能深入调试程序
;
; 生活类比：
;   高级语言就像开自动挡汽车，方便但你不知道变速箱在干嘛。
;   汇编就像开手动挡，你清楚地知道每一个齿轮的咬合。
;   当自动挡出了问题，只有懂手动挡原理的人才能真正修好它。
;
; ============================================================================
; 三、CPU的工作原理
; ============================================================================
;
; CPU（中央处理器）就像一个超级工厂：
;
;   ┌─────────────────────────────────────────────┐
;   │                   CPU                        │
;   │                                              │
;   │  ┌──────────┐  ┌──────────┐  ┌──────────┐  │
;   │  │ 控制单元  │  │ 运算单元  │  │ 寄存器组  │  │
;   │  │ (指挥官) │  │ (工人)   │  │ (工作台)  │  │
;   │  └──────────┘  └──────────┘  └──────────┘  │
;   │                                              │
;   └─────────────────────────────────────────────┘
;            │              │              │
;            ▼              ▼              ▼
;   ┌─────────────────────────────────────────────┐
;   │                  内存（仓库）                 │
;   │  存放程序代码和数据                           │
;   └─────────────────────────────────────────────┘
;
; 工作流程（取指-译码-执行循环）：
;   1. 取指：控制单元从内存中取出下一条指令
;   2. 译码：控制单元翻译这条指令，知道要做什么
;   3. 执行：运算单元执行计算，结果存到寄存器或内存
;   4. 回到第1步，继续下一条指令
;
; 这就像工厂的流水线：
;   仓库管理员（取指）-> 翻译官（译码）-> 工人（执行）-> 仓库管理员...
;
; ============================================================================
; 四、计算机中的数据表示
; ============================================================================
;
; 计算机只认识0和1（二进制），但我们有多种表示方式：
;
; 十进制（人类常用）：  42
; 二进制（计算机内部）：00101010
; 十六进制（程序员常用）：0x2A
;
; 为什么程序员爱用十六进制？
;   因为1位十六进制 = 4位二进制，转换非常方便：
;   0x2A = 0010 1010 = 42
;
; 常用进制对照表：
;   十进制  二进制      十六进制
;   0       0000        0x0
;   1       0001        0x1
;   2       0010        0x2
;   ...
;   9       1001        0x9
;   10      1010        0xA
;   11      1011        0xB
;   12      1100        0xC
;   13      1101        0xD
;   14      1110        0xE
;   15      1111        0xF
;
; 数据大小单位：
;   1 字节(Byte)  = 8 位(bit)      -> 能表示 0~255
;   1 字(Word)    = 2 字节 = 16位   -> 能表示 0~65535
;   1 双字(DWord) = 4 字节 = 32位   -> 能表示 0~4294967295
;   1 四字(QWord) = 8 字节 = 64位   -> 能表示更大的数
;
; ============================================================================
; 五、NASM简介与环境搭建
; ============================================================================
;
; NASM（Netwide Assembler）是一款免费、开源的汇编器。
; 我们用NASM语法编写汇编代码，然后用NASM编译器把它变成可执行文件。
;
; 工具链：
;   源代码(.asm) --[NASM编译]--> 目标文件(.obj) --[链接器]--> 可执行文件(.exe)
;
; 推荐工具：
;   - NASM: https://www.nasm.us/
;   - 链接器: GoLink 或 Microsoft Visual Studio 的 link.exe
;   - 调试器: x64dbg, OllyDbg, WinDbg
;
; 编译命令示例：
;   nasm -f win32 hello.asm -o hello.obj
;   golink /entry:_start hello.obj kernel32.dll user32.dll
;
; ============================================================================
; 六、第一个程序：Hello World!
; ============================================================================
;
; 下面是一个完整的Windows 32位汇编程序。
; 不要被它吓到，我会逐行解释！
;
; 程序结构：
;   1. 声明段(section)和外部函数
;   2. 定义数据（字符串等）
;   3. 编写代码（入口点开始执行）
;   4. 调用Windows API输出文字
;   5. 退出程序

; ===========================================================================
; 以下是代码部分
; ===========================================================================

; --------------------------------------------------------------------------
; 段声明
; --------------------------------------------------------------------------
; 汇编程序由多个"段"(section)组成，每个段有不同的用途：
;   .data  段 = 存放已初始化的数据（比如字符串常量）
;   .bss   段 = 存放未初始化的数据（预留空间）
;   .text  段 = 存放可执行的代码

section .data                   ; ========== 数据段开始 ==========
                                ; 这里存放程序中用到的常量和字符串

    msg     db 'Hello, World!', 0x0A, 0  ; 定义一个字符串
            ; db = Define Byte，逐字节定义数据
            ; 'Hello, World!' 是我们要显示的文字
            ; 0x0A 是换行符（ASCII码10，就是\n）
            ; 0 是字符串结束标志（C风格的空终止符）
            ;
            ; 生活类比：这就像在仓库里放了一张纸条，
            ;           上面写着"Hello, World!"，然后用一个标签(msg)标记它的位置

    msg_len equ $ - msg         ; 计算字符串长度
            ; equ = 等价指令，类似于C中的 #define
            ; $ 表示当前位置，$ - msg = 当前地址 - msg起始地址 = 字符串长度
            ; 这是一个编译时计算，不会生成任何机器指令

section .bss                    ; ========== BSS段开始 ==========
                                ; 这里预留未初始化数据的空间（本程序暂时用不到）

section .text                   ; ========== 代码段开始 ==========
                                ; 这里存放程序的可执行代码

    ; ------------------------------------------------------------------
    ; 外部函数声明
    ; ------------------------------------------------------------------
    ; 我们需要调用Windows系统的API函数来输出文字和退出程序
    ; extern 表示"这个函数不在我的代码里，是外部提供的"
    ;
    ; 就像你要寄快递，你不需要知道快递公司内部怎么运作，
    ; 你只需要知道有"寄快递"这个服务可以用。

    extern _GetStdHandle@4      ; 获取标准输出设备的句柄
            ; 句柄(handle)就像是一把"钥匙"，有了它才能操作对应的设备
            ; @4 表示这个函数接收4字节的参数（stdcall调用约定的名称修饰）

    extern _WriteConsoleA@20    ; 向控制台输出文字
            ; A 表示ANSI版本（还有W表示Wide/Unicode版本）
            ; @20 表示接收20字节的参数（5个4字节参数）

    extern _ExitProcess@4       ; 退出程序
            ; 相当于C语言的 exit()

    ; ------------------------------------------------------------------
    ; 程序入口点
    ; ------------------------------------------------------------------
    ; global _start 声明程序从这里开始执行
    ; 就像一本书的"第一章第一节"

    global _start

_start:
    ; ==================================================================
    ; 第一步：获取标准输出句柄
    ; ==================================================================
    ; 我们要往屏幕上写字，首先得拿到屏幕的"钥匙"（句柄）
    ; 函数原型：HANDLE GetStdHandle(DWORD nStdHandle)
    ;   STD_OUTPUT_HANDLE = -11  表示标准输出（屏幕）

    push    -11                 ; 把参数 -11 (STD_OUTPUT_HANDLE) 压入栈中
                                ; push = 压栈，就像在桌上放一个盘子
                                ; 栈是从高地址向低地址增长的
                                ;
                                ; 为什么要用栈传参？
                                ; 因为这是Windows API的调用约定（stdcall）
                                ; 函数会从栈上读取参数

    call    _GetStdHandle@4     ; 调用函数，返回值在 EAX 寄存器中
                                ; call = 调用函数，CPU会跳转到函数地址执行
                                ; 执行完后会回到这里继续下一条指令
                                ;
                                ; EAX 就像一个工作台上的格子，
                                ; 函数把结果（句柄）放在EAX里交给我们

    ; ==================================================================
    ; 第二步：调用 WriteConsoleA 输出文字
    ; ==================================================================
    ; 函数原型：
    ;   BOOL WriteConsoleA(
    ;       HANDLE  hConsoleOutput,  ; 控制台输出句柄
    ;       LPCVOID lpBuffer,        ; 要输出的字符串地址
    ;       DWORD   nNumberOfCharsToWrite,  ; 字符数量
    ;       LPDWORD lpNumberOfCharsWritten, ; 实际写出数量（存放地址）
    ;       LPVOID  lpReserved       ; 保留参数，填NULL
    ;   );
    ;
    ; 注意：stdcall约定下参数从右到左压栈！

    push    0                   ; 第5个参数：lpReserved = NULL (0)
                                ; 保留参数，不用管

    push    0                   ; 第4个参数：lpNumberOfCharsWritten
                                ; 我们传0表示不关心实际写了多少字符
                                ; （实际项目中应该传一个变量地址来接收结果）

    push    msg_len             ; 第3个参数：要写的字符数
                                ; msg_len 是我们之前用 equ 计算的字符串长度

    push    msg                 ; 第2个参数：字符串的地址
                                ; NASM会自动把 msg 替换成它在内存中的地址

    push    eax                 ; 第1个参数：控制台句柄
                                ; eax 里存的是上一步 GetStdHandle 的返回值

    call    _WriteConsoleA@20   ; 调用函数，输出文字到屏幕
                                ; 如果成功，返回非零值

    ; ==================================================================
    ; 第三步：退出程序
    ; ==================================================================
    ; 程序干完了该干的事，该"下班"了

    push    0                   ; 退出码 = 0（表示正常退出）
                                ; 0 通常表示"一切正常"
                                ; 非零值通常表示某种错误

    call    _ExitProcess@4      ; 调用 ExitProcess 退出程序
                                ; 程序到此结束，后面的代码不会执行

    ; ==================================================================
    ; 如果到这里说明 ExitProcess 失败了（几乎不可能发生）
    ; 但为了代码完整性，我们也处理一下
    ; ==================================================================
    ret                         ; ret = return，返回（虽然这里没什么可返回的）

; ============================================================================
; 练习题
; ============================================================================
;
; 练习1（概念题）：
;   汇编语言和机器语言是什么关系？和高级语言（如C）是什么关系？
;
; 练习2（概念题）：
;   请说出CPU执行指令的三个主要步骤。
;
; 练习3（计算题）：
;   请将以下数据转换：
;   a) 十进制 255 -> 二进制 -> 十六进制
;   b) 十六进制 0x1F -> 十进制 -> 二进制
;   c) 二进制 11001010 -> 十六进制 -> 十进制
;
; 练习4（代码题）：
;   修改上面的程序，让它输出你的名字（提示：只需要修改 msg 的内容）
;
; 练习5（思考题）：
;   为什么 Windows API 的参数要从右到左压栈？（提示：想想栈的结构）
;
; ============================================================================
; 参考答案
; ============================================================================
;
; 答案1：
;   汇编语言是机器语言的助记符表示，一一对应。
;   每条汇编指令 = 一条机器指令。
;   高级语言会被编译器翻译成汇编/机器语言。
;   关系链：高级语言 -> (编译器) -> 汇编语言 -> (汇编器) -> 机器语言
;
; 答案2：
;   取指（从内存读取指令）-> 译码（翻译指令含义）-> 执行（执行指令操作）
;   然后回到取指，循环往复。
;
; 答案3：
;   a) 255 = 11111111 = 0xFF
;   b) 0x1F = 31 = 00011111
;   c) 11001010 = 0xCA = 202
;
; 答案4：
;   把 msg 那一行改成：
;   msg db '你的名字', 0x0A, 0
;   同时更新 msg_len（或者用 $ - msg 自动计算）
;
; 答案5：
;   因为栈是从高地址向低地址增长的。参数从右到左压栈后，
;   栈顶就是第一个参数（最左边的），函数可以直接用 [esp] 读取第一个参数，
;   [esp+4] 读取第二个参数，依此类推。这样函数访问参数更方便。
;
; ============================================================================
; 教授的话
; ============================================================================
;
; 【核心收获】
;
;   1. 汇编语言 = 机器语言的助记符，一一对应
;   2. CPU工作循环 = 取指 -> 译码 -> 执行
;   3. 数据表示：二进制(bit)、十六进制(0x)、字节(Byte=8bit)
;   4. 第一个程序：调用Windows API输出 Hello World
;   5. 栈传参：stdcall约定下参数从右到左压栈
;
; 【常见陷阱】
;
;   1. 汇编不等于机器码，还需要汇编器(NASM)翻译
;   2. 字符串末尾的 0 (NULL) 容易忘记，导致输出乱码
;   3. push 的顺序和参数声明顺序相反（从右到左）
;   4. extern 声明的函数名必须和链接器中的一致（含 @N 后缀）
;
