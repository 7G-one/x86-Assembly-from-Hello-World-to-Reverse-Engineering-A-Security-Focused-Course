; 第03课：数据传送指令
; MOV/MOVZX/MOVSX/XCHG/PUSH/POP——数据的搬运工
;
; ============================================================================
; 一、MOV 指令 - 最基本的传送
; ============================================================================
;
; 语法：MOV 目标, 源
; 作用：把"源"的数据复制到"目标"（源不变）
;
; 生活类比：
;   MOV就像复印机——你把原件（源）放进去，得到一份复印件（目标）。
;   原件还在，不会消失。
;
; 合法的MOV操作：
;   MOV 寄存器, 寄存器      ; 寄存器 -> 寄存器
;   MOV 寄存器, 立即数      ; 常数 -> 寄存器
;   MOV 寄存器, 内存        ; 内存 -> 寄存器
;   MOV 内存, 寄存器        ; 寄存器 -> 内存
;   MOV 内存, 立即数        ; 常数 -> 内存
;   MOV 段寄存器, 寄存器    ; 寄存器 -> 段寄存器
;
; 不合法的MOV操作：
;   MOV 内存, 内存          ; 不能直接内存到内存！必须经过寄存器中转
;   MOV CS, ...             ; 不能直接修改CS
;   MOV 立即数, ...         ; 立即数不能作为目标（常数不能被赋值）
;
; 生活类比：
;   "不能直接从仓库A搬到仓库B" —— 你得先把东西搬到工作台上（寄存器），
;   再从工作台搬到仓库B。虽然多了一步，但这是规则。

section .data
    var1    dd 100
    var2    dd 200
    var3    dd 0
    msg_ok  db 'Done!', 0

section .bss
    temp    resd 1

section .text
    extern _ExitProcess@4
    global _start

_start:
    ; ==================================================================
    ; MOV 基本用法
    ; ==================================================================

    ; 寄存器 <- 立即数
    mov     eax, 42             ; EAX = 42
    mov     ebx, 0xDEADBEEF    ; EBX = 0xDEADBEEF（经典调试值）
    mov     cl, 'A'             ; CL = 65 (字符'A'的ASCII码)

    ; 寄存器 <- 寄存器
    mov     edx, eax            ; EDX = EAX = 42
    mov     ch, cl              ; CH = CL = 65

    ; 寄存器 <- 内存
    mov     eax, [var1]         ; EAX = 内存[var1]处的值 = 100
    mov     bx, [var2]          ; BX = 内存[var2]处的低16位

    ; 内存 <- 寄存器
    mov     [var3], eax         ; 内存[var3] = EAX = 100

    ; 内存 <- 立即数
    mov     dword [temp], 999   ; 内存[temp] = 999
                                ; 必须指定大小（dword/word/byte）

    ; ==================================================================
    ; 大小匹配规则
    ; ==================================================================
    ; 源和目标的大小必须一致！

    mov     eax, 0x12345678     ; 32位立即数 -> 32位寄存器 ✓
    mov     ax, 0x1234          ; 16位立即数 -> 16位寄存器 ✓
    mov     al, 0x12            ; 8位立即数 -> 8位寄存器 ✓

    ; 以下会出错（大小不匹配）：
    ; mov eax, 0x1234567890  ; 错误！立即数超过32位
    ; mov al, 0x1234         ; 错误！16位立即数不能放入8位寄存器

    ; 大小修饰符的使用
    mov     byte [temp], 0      ; 明确指定操作1字节
    mov     word [temp], 0      ; 明确指定操作2字节
    mov     dword [temp], 0     ; 明确指定操作4字节

    ; ==================================================================
    ; MOVZX 和 MOVSX - 带扩展的传送
    ; ==================================================================
    ; 当源比目标小时，需要扩展：
    ;   MOVZX = 零扩展（高位补0）
    ;   MOVSX = 符号扩展（高位补符号位）

    ; MOVZX 示例（零扩展）
    mov     al, 0xFF            ; AL = 255 (11111111)
    movzx   eax, al             ; EAX = 0x000000FF = 255
                                ; 高24位全部补0
                                ;
                                ; 生活类比：把小盒子里的东西放到大盒子里，
                                ;           多出来的空间用0填充

    mov     bl, 0x80            ; BL = 128 (10000000)
    movzx   ebx, bl             ; EBX = 0x00000080 = 128

    ; MOVSX 示例（符号扩展）
    mov     al, 0xFF            ; AL = 0xFF
    movsx   eax, al             ; EAX = 0xFFFFFFFF = -1
                                ; AL的最高位是1（负数），所以高24位全部补1
                                ;
                                ; 有符号解读：0xFF = -1
                                ; 所以 MOVSX 保持了数值的正确性

    mov     cl, 0x7F            ; CL = 127 (01111111)
    movsx   ecx, cl             ; ECX = 0x0000007F = 127
                                ; 最高位是0（正数），所以高24位补0

    ; 为什么要区分MOVZX和MOVSX？
    ; 因为同一个二进制数，无符号和有符号的解读不同：
    ; 0xFF: MOVZX -> 255（无符号），MOVSX -> -1（有符号）

    ; ==================================================================
    ; LEA 指令 - 加载有效地址
    ; ==================================================================
    ; LEA 目标, [地址表达式]
    ; 计算地址表达式的值，放入目标寄存器（不访问内存！）

    lea     eax, [var1]         ; EAX = var1的地址（不是var1的值！）
                                ; 等价于 mov eax, var1

    ; LEA 的数学计算能力（不访问内存，纯计算）
    mov     ebx, 10
    lea     eax, [ebx + ebx*4]  ; EAX = EBX * 5 = 50
                                ; 这是一条指令完成了乘法！

    lea     eax, [ebx*8 + 3]   ; EAX = EBX * 8 + 3 = 83

    mov     ecx, 7
    lea     eax, [ebx + ecx*2 + 10] ; EAX = 10 + 7*2 + 10 = 34

    ; ==================================================================
    ; XCHG 指令 - 交换
    ; ==================================================================
    ; XCHG 操作数1, 操作数2
    ; 交换两个操作数的值

    mov     eax, 111
    mov     ebx, 222
    xchg    eax, ebx            ; 交换！EAX = 222, EBX = 111
                                ;
                                ; 生活类比：两个人交换手中的牌
                                ; 不需要临时变量，一步完成

    ; XCHG 也可以操作内存
    xchg    eax, [var1]         ; 交换EAX和内存[var1]的值

    ; ==================================================================
    ; PUSH 和 POP - 栈操作
    ; ==================================================================
    ; PUSH 源：把源的值压入栈（ESP减4，然后存值）
    ; POP  目标：从栈顶弹出值到目标（取值，然后ESP加4）
    ;
    ; 生活类比：
    ;   PUSH = 往一摞盘子上面放一个盘子
    ;   POP  = 从一摞盘子上面拿走一个盘子
    ;   后放的先拿（后进先出 LIFO）

    mov     eax, 100
    mov     ebx, 200

    push    eax                 ; 栈：[100]    ESP减4
    push    ebx                 ; 栈：[200][100] ESP再减4
    push    300                 ; 栈：[300][200][100] ESP再减4

    pop     ecx                 ; ECX = 300   ESP加4
    pop     edx                 ; EDX = 200   ESP加4
    pop     esi                 ; ESI = 100   ESP加4
    ; 注意：pop的顺序和push相反！

    ; PUSH/POP 常用于：
    ; 1. 函数调用时传递参数
    ; 2. 保存和恢复寄存器的值
    ; 3. 临时保存数据

    ; 保存和恢复寄存器的例子
    mov     eax, 0xAAAA         ; EAX有重要数据
    push    eax                 ; 先保存起来
    ; ... 这里可能修改了EAX ...
    mov     eax, 999            ; EAX被修改了
    pop     eax                 ; 恢复！EAX = 0xAAAA
                                ; 这就是"保存现场"

    ; ==================================================================
    ; 综合示例：交换两个内存变量的值
    ; ==================================================================

    ; var1 和 var2 的值交换
    mov     eax, [var1]         ; EAX = var1的值
    mov     ebx, [var2]         ; EBX = var2的值
    mov     [var1], ebx         ; var1 = EBX（原来的var2）
    mov     [var2], eax         ; var2 = EAX（原来的var1）

    ; 退出程序
    push    0
    call    _ExitProcess@4

; ============================================================================
; 练习题
; ============================================================================
;
; 练习1（代码题）：
;   写一段代码，把值 42 存入 EAX，然后把 EAX 的值存入地址为 myvar 的内存。
;
; 练习2（概念题）：
;   以下哪些MOV指令是合法的？哪些不合法？为什么不合法？
;   a) mov eax, ebx
;   b) mov [var1], [var2]
;   c) mov al, 300
;   d) mov eax, [ebx]
;   e) mov 5, eax
;
; 练习3（代码题）：
;   写一段代码，使用PUSH/POP把EAX、EBX、ECX的值保存起来，
;   然后在不需要这三个寄存器的代码之后恢复它们（注意顺序）。
;
; 练习4（概念题）：
;   MOVZX 和 MOVSX 有什么区别？写出以下操作的结果：
;   mov al, 0x80
;   movzx eax, al   ; EAX = ?
;   movsx  ebx, al  ; EBX = ?
;
; 练习5（LEA题）：
;   不使用 MUL 或 IMUL 指令，用 LEA 计算 EAX = EBX * 9。
;   （提示：9 = 8 + 1）
;
; ============================================================================
; 参考答案
; ============================================================================
;
; 答案1：
;   mov eax, 42
;   mov [myvar], eax
;   注意不能写 mov [myvar], 42 除非指定大小：mov dword [myvar], 42
;
; 答案2：
;   a) 合法：寄存器到寄存器
;   b) 不合法：不能直接内存到内存
;   c) 不合法：300超过255，放不进8位寄存器AL
;   d) 合法：EBX的值作为地址，取该地址的内容放入EAX
;   e) 不合法：立即数不能作为目标
;
; 答案3：
;   push eax
;   push ebx
;   push ecx
;   ; ... 使用这些寄存器的代码 ...
;   pop ecx    ; 注意顺序：后进先出！
;   pop ebx
;   pop eax
;
; 答案4：
;   MOVZX是零扩展，MOVSX是符号扩展
;   0x80 = 10000000 (二进制)
;   movzx eax, al  -> EAX = 0x00000080 = 128
;   movsx  ebx, al -> EBX = 0xFFFFFF80 = -128
;
; 答案5：
;   mov ebx, 5
;   lea eax, [ebx + ebx*8]  ; EAX = EBX + EBX*8 = EBX*9 = 45
;
; ============================================================================
; 教授的话
; ============================================================================
;
; 【核心收获】
;
;   1. MOV 是复制不是移动（源不变），不能内存到内存直接传送
;   2. 源和目标大小必须匹配（32位对32位，8位对8位）
;   3. MOVZX 零扩展（高位补0），MOVSX 符号扩展（高位补符号位）
;   4. LEA 只做地址计算不访问内存，可用于高效数学运算（如 eax = ebx*5）
;   5. XCHG 一步交换两个值，不需要临时变量
;   6. PUSH/POP 操作栈，后进先出(LIFO)
;
; 【常见陷阱】
;
;   1. 不能 mov [mem1], [mem2]，必须经过寄存器中转
;   2. 不能把大值塞进小寄存器（如 mov al, 0x1234 报错）
;   3. PUSH/POP 必须配对，否则栈不平衡导致程序崩溃
;   4. LEA 不访问内存！lea eax, [ebx+4] 不会读取 [ebx+4] 的内容
;
